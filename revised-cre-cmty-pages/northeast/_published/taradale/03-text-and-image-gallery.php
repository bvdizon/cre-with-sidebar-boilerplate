    <h2 class="sect-title">About Taradale</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Established in 1984, the community in Taradale is one of the most desirable residential neighborhoods in the whole of Calgary. </p>
            <p class="par-lg">Taradale is bounded by the Falconbridge Blvd on the west, 64 Avenue N on the south, 68 Street E on the east and the Taradale Drive on the north.</p>
            <p class="par-lg">Homes for sale in Taradale comprise solely of single-family detached homes that majorly attract first time home buyers and investors. </p>
            <p class="par-lg">On average the listing price of these homes is $400,000.</p>
