    <h2 class="sect-title">Community Vibe in Taradale</h2>
    <div class="col-grid-2">
        <div>

            <p class="par-lg">A charming residential neighborhood in the North-east quadrant of Calgary.</p>
            <p class="par-lg">Located only a kilometer away from the famous Prairie Winds Park, the <span class="par-xl">Taradale Real Estate offers plenty of green spaces for the residents to enjoy their evening walks and sports activities</span>. </p>
            <p class="par-lg">The community offers a variety of recreational venues including a community centre, soccer fields, outdoor hockey rink, ball diamonds, basketball courts and so on for the residents to make the most of their leisure hours. </p>
