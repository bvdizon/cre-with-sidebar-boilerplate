    <h2 class="sect-title">Community Vibe in Vista Heights</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">A highly sought after residential community located in the north east quadrant of Calgary, <span class="par-xl">Vista Heights is a dream neighborhood for single professionals and modern families alike</span>. </p>
            <p class="par-lg">While The Crossroads Community Association in the neighborhood offers abundant recreational opportunities such as crafts, soccer, and Blockwatch programs, the Fifty Plus Club organizes a bunch of fun and exciting tours and dinner theaters as well.</p>
