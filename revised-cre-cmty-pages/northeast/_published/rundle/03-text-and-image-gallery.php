    <h2 class="sect-title">About Rundle</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Established in 1973, the community in Rundle was named after a 19th-century missionary called Robert Terrill Rundle who worked in Western Canada. </p>
            <p class="par-lg">A part of the four communities together referred to as ‘The Properties’, Rundle is a master-planned residential neighborhood in North East Calgary. </p>
            <p class="par-lg">The community is flanked by 36 Street E on the west, 52 Street E on the east, the Trans-Canada Highway on the south and the 32 Avenue N on the north.</p>
