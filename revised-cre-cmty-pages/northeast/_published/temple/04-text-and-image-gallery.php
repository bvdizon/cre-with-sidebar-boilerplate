    <h2 class="sect-title">Community Vibe in Temple</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">A low density peaceful residential neighborhood located in the North-East quadrant of Calgary, Temple is an ideal community for growing families, young couples and retirees alike. </p>
            <p class="par-lg"><span class="par-xl">The highly active community association offers a myriad of recreational opportunities for the residents</span> in the form of basketball and tennis courts, an all-season arena, fitness centers and even a wave pool at the nearby Village Square Leisure Centre.</p>
