    <h2 class="sect-title">About Whitehorn</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Ever since its establishment in the early seventies, the community in Whitehorn has been attracting both first-time homebuyers as well as experienced real estate investors. </p>
            <p class="par-lg">With the world-renowned Peter Lougheed Centre located just south-west of it, the community is bounded by the McKnight Boulevard on the north and the 52 Street E on the east.</p>
            <p class="par-lg">Homes for sale in Whitehorn majorly include single-family detached and semi-detached units that are readily available at an average sale price of $360,000.</p>
