    <div>
        <div>
            <p class="par-lg">Established in the early sixties, the community in Mayland Heights was originally known as Crossroads and is currently represented by the Ward 3 Councilor in the Calgary City Council. </p>
            <p class="par-lg">Mayland Heights is bordered by the Trans-Canada Highway on the north the Deerfoot Trail on the west, Memorial Drive to the south and the Barlow Trail on the east.</p>
