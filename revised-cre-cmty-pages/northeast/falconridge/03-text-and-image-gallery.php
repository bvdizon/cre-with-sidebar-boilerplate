    <h2 class="sect-title">About Falconridge</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Located deep within the north-east quadrant of Calgary, the Falconridge community is bordered by the McKnight Boulevard on the south, 68 Street E on the east, 64 Avenue N on the north and the 44 Street E on the west.</p>
            <p class="par-lg">The neighborhood was established in 1979 and is represented by the Ward 5 Councilor in the Calgary City Council.</p>

