    <h2 class="sect-title">About Douglasdale/Glen</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Established in 1986, the community in Douglasdale/Glen is represented by the Ward 12 Councilor in the Calgary City Council.</p>
            <p class="par-lg">The community is bounded by the Bow River on the west, the Deerfoot Trail on the east, 130 Avenue S on the south and 114 Avenue S on the north.</p>
            <p class="par-lg">Homes for sale in Douglasdale/Glen majorly comprise single-family detached homes that are regularly listed at an average price of around mid 300s up to a million for the premium units.</p>
