    <h2 class="sect-title">Community Vibe in Rocky Ridge</h2>
    <div class="col-grid-2">
        <div>
        <p class="par-lg">Demographics for Rocky Ridge indicate that the population, according to the 2012 municipal census, was estimated to be around 7, 662 individuals residing in 2,972 dwellings.</p>
        <p class="par-xl">Rocky Ridge homes for sale are always on demand due to the neighborhood’s sense of community.</p>

        <p class="par-lg">Compared to the previous year’s numbers, the increase is pegged at 5.5%.</p>

        <p class="par-lg">Most of the houses in Rocky Ridge are single family detached homes with 9.1% of them being rented.</p>

