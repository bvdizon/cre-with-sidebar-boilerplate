    <h2 class="sect-title">About University Heights</h2>
    <div class="col-grid-2">
        <div>
        <p class="par-lg">A well knit community within close proximity of the University of Calgary.<p>
            <p class="par-xl"> University Heights homes boast of a prime location that has become a major housing hot spot for many real estate buyers and sellers in NW Calgary area.</p>
        <p class="par-lg">Built in the year 1963, the community in University Heights has also garnered considerable attention given the fact that the erstwhile Prime Minister Stephen Harper stayed in the neighborhood during his University days.</p>
        <p class="par-lg">The University Heights homes for sale comprise of a variety of dwellings such as townhouses, condominiums and single family homes of different sizes and styles. Many homes have undergone considerable remodeling and renovation over the years.
