        <div>
        <P class="par-lg">Ranchlands homes are situated in northwest Calgary. It is a peaceful neighborhood that was established in 1971 after it came under the City of Calgary in 1961. The neighborhood is represented by the Ward 2 Councilor at the Calgary City Council.</P>
        <P class="par-xl">Ranchlands homes for sale are always on demand due to the neighborhoodâ€™s spectacular environmental reserve and abundance of recreational activities.</P>
        <P class="par-lg">Ranchlands is spread across 2.3 square kilometers of land, while the population density is estimated to be around 3,320 per kilometer square. 9% of the houses in Ranchlands are either condominiums or apartments. 27.4% of the houses have been rented out.</P>

        <P class="par-lg">Ranchlands demographics, according to the 2012 municipal census, indicate that the neighborhood is home to 7,626 residents. These residents occupy 3,051 dwellings. The population has grown by 1.3% considering the fact that the population used to be at 7,529 according to the 2011 census.</P>

