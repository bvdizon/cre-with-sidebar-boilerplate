    <h2 class="sect-title">About Arbour Lake</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">In 1991, the very first Arbour Lake show-home opened it's doors, wowing homebuyers right
                from the very
                start. By 1993, the lake was ready for residents to enjoy water activities for the first time ever in
                the
                Northwest of Calgary!</p>
            <p class="par-xl">Arbour Lake has some of the most amazing residential properties in Northwest Calgary.</p>

            <p class="par-lg">This residential neighbourhood is comprised of about 70% single-family dwellings, of which
                about 87% are
                owner-occupied. Apartments and townhomes represent about 25% of Arbour Lake homes for sale, giving
                homebuyers plenty of options to choose from.</p>

            <p class="par-lg">Homebuyers will find an abundance of 2+ car garages, big picture windows, and
                well-maintained yards.
                Interiors range in styles, where most capitalize on open, airy spaces with giving that play well to
                today's
                open-modern styles.</p>


