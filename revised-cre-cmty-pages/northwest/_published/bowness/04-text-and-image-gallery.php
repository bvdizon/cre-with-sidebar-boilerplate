    <h2 class="sect-title">Community Vibe in Bowness </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-xl">Residents in Bowness get to enjoy the peace and quiet of a residential area with few
                commercial spaces and
                many parks, as well as convenient access to some of Calgary's best amenities just beyond its borders.
            </p>
            <p class="par-lg">A short distance outside of Bowness, highlights include: Market Mall, Bowmont Park,
                Shouldice Athletic Park,
                The University of Calgary, Alberta Children's Hospital, Winsport Winter Sport Institute.</p>
            <p class="par-lg">Surrounded by serene parks, convenient commuter routes, and some of Calgary's best
                attractions, the neighborhood of Bowness is full of real estate opportunities to suit every need and
                taste. From its stately riverside properties, to the spacious mid-century bi-levels, and affordable
                condos, the Bowness property market offers a fantastic array of homes in a well-established area.</p>
            <p class="par-lg">Bowness Park is perhaps the biggest community attraction. This expansive park is located
                on the shores of Bow River and offers residents opportunities for paddleboating, picnics, barbecues,
                jogging, biking, and
                skating in the winter.</p>

            <p class="par-lg">The Bowness Community Association also runs an indoor skating facility and sports field,
                and organizes a host of year-round community events.</p>
