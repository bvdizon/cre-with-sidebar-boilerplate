    <h2 class="sect-title">About Bowness </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Bowness' life as a residential community began with its sale to property developer John
                Hextall in 1908.
                Hextall proceeded to build a bridge, organize streetcar access, and subdivide the existing property into
                a
                collection of lots he called Bowness Estates.</p>

            <p class="par-lg">Homes for sale in Bowness vary between up-market luxury estates, budget condos, and
                vintage single-family
                homes. As such, you'll find facades that display charming traditional-cottage, Craftsman, and
                Modern-bungalow styles, across a wide spectrum of price points. On most properties, beautiful lawns lead
                up to raised porches with covered entries, and inside you can expect to find spacious, well-lit
                basements.</p>

            <p class="par-xl">Residents enjoy the beautiful Bowness Park, and the convenient access to some of Calgary's
                finest
                attractions and amenities.</p>

            <p class="par-lg">Real estate in Bowness is a diverse mix of luxury homes along the shores of the Bow River,
                and various
                new-traditional and Mid-Century single-family homes further from the water.</p>

            <p class="par-lg">First time buyers, or those who need a more compact space, can choose from a number of
                condos and
                semi-detached homes found throughout Bowness. Throughout the area, mature trees line the spacious
                front-lawns, which remain mostly unobstructed thanks to presence of rear alleys for convenient access
                and
                storage.</p>


