    <h2 class="sect-title">About Hawkwood </h2>
    <div class="col-grid-2">
        <div>
        <p class="par-lg">Hawkwood is a residential neighborhood that was established in 1981 along the North West section of Calgary. The neighborhood is bordered by the Nose Hill Drive on the west, the John Laurie Boulevard on the south, the Sarcee Trail on the east and the Country Hills Boulevard on the north. Hawkwood is represented by the Ward 2 councilor in the Calgary City Council.</p>

        <p class="par-lg">Homes for sale in Hawkwood are disappearing quickly as there are 2 quality elementary schools in the area, several parks and family spaces. For a quiet city, itâ€™s proximity to Calgary makes it a cultural attraction. Music lovers will the neighborhood as it hosts a ton of musical events every year. The Hawkwood Community Association also hosts events on a regular basis.</p>

        <p class="par-xl">One of the real estate hotspots for contemporary homebuyers an investors alike, the Hawkwood homes for sale comprise chiefly of estate homes, mid-range homes and adult condominiums for retirees, with the average pricing of single family residential units at $550,000.</p>

