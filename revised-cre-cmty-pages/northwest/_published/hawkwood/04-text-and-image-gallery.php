    <h2 class="sect-title">Community Vibe in Hawkwood </h2>
    <div class="col-grid-2">
        <div>
        <p class="par-lg">Offering some of the most breathtaking views of the Rocky Mountains and the city skyline, the community in Hawkwood is named after its original landowners Joseph and John Hawkwood.</p>
        <p class="par-xl">Hawkwood Real estate enjoys a gently sloping geography which lends the community a dream-like appeal.</p>
        <p class="par-lg">With its scenic walking trails, immaculate greens and lovely fountains and ponds, Hawkwood provides a charming and peaceful neighborhood for joggers, children and dog walkers alike. Apart from this, the community offers a range of recreational activities including playgrounds, parks, fields, shopping plazas and such for the local residents to enjoy their spare time.</p>
        <p class="par-lg">Country Hills Boulevard is close to Hawkwood and provides the perfect opportunity for residents to travel to Calgary. Residents and new buyers alike can be assured that they have all the amenities they need, within driving distance with various shops, restaurants and entertainment centers close by.</p>


