    <h2 class="sect-title">Community Vibe in Royal Oak</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">The community in Royal Oak also consists of a huge clubhouse which caters to the recreational needs of its inhabitants. </p>
            <p class="par-xl">It offers a variety of fun activities and facilities including tennis courts, volleyball courts, playgrounds and even a pond.</p>

            <p class="par-lg">Shopping in Royal Oak can be enjoyed at the nearby Royal Oak Shopping Center which is only a few minutes away from a newly built LRT station.</p>

            <p class="par-lg">Schools in Royal Oak include the Royal Oak School, William D. Ratt Middle School and many more.</p>



