    <h2 class="sect-title">About Silver Springs</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Located within the lush green surroundings of the Bow River’s north embankment,The community also offers some of the most breathtaking panoramic views of the surrounding Rocky Mountains and Canada Olympic Park.</p>
            <p class="par-xl">Silver Springs is a beautiful and tranquil neighborhood in the northwestern part of Calgary. </p>

            <p class="par-lg">The community in Silver Springs was first established in 1972 and has been a highly active neighborhood consisting of large parks, sports programs and a few of Calgary’s only remaining outdoor pools, ever since.</p>

            <p class="par-lg">The Silver Springs homes for sale include a variety of properties such as townhomes, detached homes, and duplexes of several different sizes and styles.</p>
