    <h2 class="sect-title">About Ranchlands</h2>
    <div class="col-grid-2">
        <div>
        <p class="par-lg">Complete with beautiful tree-lined streets, mystical winding trails, rustic residential units finished off earthy tones and a central location within the neighborhood.</p>
        <p class="par-xl">The community in Ranchlands enjoys the reputation of being among the favorite destinations of most contemporary homebuyers and investors alike.</p>

        <p class="par-lg">With the Crowfoot arena and Crowfoot YMCA located minutes away, Ranchland real estate offers an abundance of recreational activities for its local inhabitants. From outdoor skating rinks, sports fields to tennis courts, Dan Scott Dance Studio, and a spectacular environmental reserve there is something in Ranchlands for everyone.</p>

        <p class="par-lg">Ranchlands homes for sale comprise of an eclectic mix of housing options from single family detached homes to apartments and stylish condo units.</p>

