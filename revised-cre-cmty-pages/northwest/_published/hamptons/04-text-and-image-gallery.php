    <h2 class="sect-title">Community Vibe in Hamptons </h2>
    <div class="col-grid-2">
        <div>
        <p class="par-lg">While The Hamptons was officially established as a residential community in 1990, the neighbourhood has a history dating back to the mid-1970s. The southern section of The Hamptons was first annexed in 1976, but the northern section located north of Edgemont was not annexed until 1989.</p>

        <p class="par-xl">Nowadays, the Hamptons is known for its stunning real estate options, as well as its world-famous annual Stampede breakfast and Olympic-sized hockey rink.</p>
        <p class="par-lg">The Hamptons experienced its first residential development in the early 2000s, and the majority of the lavish real estate in The Hamptons today was built after 2005.</p>


