    <h2 class="sect-title">About Hamptons </h2>
    <div class="col-grid-2">
        <div>
        <p class="par-lg">Homes for sale in The Hamptons were built to surround the renowned 18-hole Hamptons Golf course that has hosted many famous golfers throughout the years, like Arnold Palmer and Annika Sorenstam.</P>

        <p class="par-xl">A stroll through The Hamptons will reveal gorgeously manicured fairways and greens, sparkling ponds, and natural ravines in every direction.</P>
        <p class="par-lg">While avid golfers are a natural fit for this famed community, The Hamptons is also home to several tennis courts, baseball diamonds, soccer fields, playgrounds, and shopping centres to meet the needs and interests of every resident.</P>

        <p class="par-lg">Locals flock to Country Hills Village for a thrilling day of shopping, while the nearby Crowfoot Towne Center also features acclaimed restaurants, auto dealers, a Safeway, and a Revy. For a weekend adventure, residents also enjoy a trip to West Edmonton Mall, located less than 3 hours from The Hamptons real estate.</P>
