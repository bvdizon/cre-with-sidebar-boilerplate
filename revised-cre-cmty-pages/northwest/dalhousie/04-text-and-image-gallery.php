    <h2 class="sect-title">Community Vibe in Dalhousie </h2>
    <div class="col-grid-2">
        <div>
        <p class="par-lg">One of Calgary’s more mature neighborhoods, the community in Dalhousie enjoys an impressive central location with close proximity to top-notch educational institutions, recreational venues, shopping complexes and even the University of Calgary.</p>
        <p class="par-lg">Dalhousie homes are located in Northwest Calgary. Dalhousie connected towards the North via John Laurie Boulevard, towards the south via Crowchild Trail and to the west via Sarcee Trail and east via Shaganappi Trail.</p>

        <p class="par-lg">The Ward 7 Councilor represents Dalhousie in the Calgary City Council.</p>

        <p class="par-xl">Another strong feature is the neighborhood’s high level of community engagement.</p>
        <p class="par-lg">Some key community events here include the events hosted by the Calgary Folk Club, sports programs and several other seasonal community gatherings.</p>


