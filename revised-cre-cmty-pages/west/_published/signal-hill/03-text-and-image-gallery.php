    <h2 class="sect-title">About Signal Hill</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Established in 1986, the community is bound by the <a href="/springbank-hill.php">Springbank Hill</a> community on the west, the Glenmore Trail on the south, the 17th Ave on the north and the Sarcee trail on the east.</p>
            <p class="par-lg">Located in the center of the Broadcast Hill in Calgary, the Signal Hill community comprises of two residential neighborhoods namely the Signal Ridge and the Sienna Hills. </p>

