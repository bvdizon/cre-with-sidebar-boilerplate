    <h2 class="sect-title">About Tuxedo Park</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">One of the oldest of Calgary's settlements, Tuxedo Park has a special place in the history of the region.</p>
            <p class="par-lg">This history can be seen in the form of the many antiquated buildings, schools, churches and farmhouses that dot Tuxedo Park.</p>

            <p class="par-lg">The average asking price for a house in Tuxedo Park is $600,000.</p>
            <p class="par-lg">Houses include most type of housing including single-family homes, condos, townhouses and so on.</p>
            <p class="par-lg">The settlement is very well connected with a well-laid network of roads.</p>


