    <h2 class="sect-title">Community Vibe in Meadowlark Park</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Meadowlark Park Community association administers the community and operates a community centre, complete with standard kitchen and stage, with wheelchair access to the building</p>
            <p class="par-lg">Chinook Centre is located east of the community and is Calgary’s largest indoor shopping centre, with over 250 stores and Scotiabank Theatre complex</p>
            <p class="par-lg">Besides the food court in the mall, there is also McSorley’s Pub and Chop Steakhouse are fancy restaurants located around the mall, with there also being a Red Lobster and Ricky’s All Day Grill across the street.</p>

