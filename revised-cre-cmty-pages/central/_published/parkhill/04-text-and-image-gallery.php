    <h2 class="sect-title">Community Vibe in Parkhill </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-xl">Parkhill Real Estate is renowned for its rich cultural heritage and historical character
                that distinguishes it from the other communities in town. </p>
            <p class="par-lg">Set against the backdrop of beautiful tree-lined streets and pretty open spaces, the homes
                for sale in Parkhill majorly comprise of single family detached homes and apartment condominiums that
                are regularly listed at a starting price of around $180,000 and climb as high as $2 million for the more
                premium properties. </p>
            <p class="par-lg">The Stanley Park located within the community offers plenty of recreational opportunities
                in the form of a pebbly beach, an outdoor pool and large open spaces.</p>


