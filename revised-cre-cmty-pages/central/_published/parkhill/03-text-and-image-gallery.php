    <h2 class="sect-title">About Parkhill </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Located in the south-west quadrant of Calgary,</p>
            <p class="par-xl">The Parkhill community is a quaint residential neighborhood bounded by the MacLeod Trail
                on the east, the Elbow River on the west, Mission Road on the north and the Crescent Boulevard on the
                south.</p>
            <p class="par-lg">One of the oldest neighborhoods in Calgary, the community in Parkhill was established in
                1910 with an area development plan in place. </p>

