    <h2 class="sect-title">Community Vibe in Mayfair </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-xl">Mayfair is located close to Chinook Centre, Calgary’s largest indoor shopping centre, </p>
            <p class="par-lg">With over 250 stores and boutiques, a food court approximately the size of a football
                field and a multiplex cinema showing the latest releases. Chinook has stores aimed at all genders and
                fashion preferences. Apart from the food court, there is also McSorley’s Pub and Chop Steakhouse, as
                well as Ricky’s All Day Grill and Red Lobster being located across McLeod Trail.</p>

            <p class="par-lg">North of the community in is the Calgary Golf and Country Club, Alberta’s oldest private
                golf course, which has a magnificent view of the Glenmore Reservoir. Close by is North Glenmore Park and
                Weaselhead, excellent spots for jogging, hiking, picnics and bbq’s.</p>

            <p>Mayfair is home to a private school.</p>

