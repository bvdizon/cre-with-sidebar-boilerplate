    <h2 class="sect-title">About Mayfair </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Mayfair is located in Ward 11 and represented by Brian Pincott on the Calgary City
                Council. </p>
            <p class="par-xl">Provincially, Mayfair is in the Calgary-Elbow riding, represented by Alberta Premier
                Alison Redford and federally in the Calgary Centre riding, with no representation following the
                resignation of Lee Richardson.</p>


