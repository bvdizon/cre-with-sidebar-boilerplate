    <h2 class="sect-title">About Hillhurst</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">A vibrant and dynamic residential neighborhood located within the inner city of Calgary just north of the City Downtown and Bow River, Hillhurst is the perfect place to live, shop and play! With a glorious history dating back to the 1914, Hillhurst features a fascinating old world charm that is reflected in its architecture as well as landscaping.</p>

            <p class="par-lg">With a population of around 5000, the community in Hillhurst is popular for its beautiful tree-lined streets, small well kept homes and paved pathways. </p>
            <p class="par-lg">Typically available at an average price of around $960,000, homes for sale in Hillhurst include a variety of budget options ranging from small starter homes for young couples and bachelors to palatial condominiums for the wealthy and influential. </p>
            <p class="par-lg">A number of recreational activities such as squash courts, gymnasiums, climbing walls and Olympic Ovals are provided by the nearby University of Calgary.</p>

            <p class="par-lg">Hillhurst and West Hillhurst are Northwest Calgary inner city neighbourhoods, located on the Bow River beside the trendy shops and restaurants of Kensington. </p>
            <p class="par-lg">Original homes date back to 1907 when the community was first developed and new homes were built. </p>
            <p class="par-lg">The area is split into 2 sections; west of 19th street is known as West Hillhurst, and Hillhurst runs eastward to 14th street. </p>
            <p class="par-lg">Close to downtown Calgary’s established subdivision, Hillhurst homes have been turned into inner-city in-fills. </p>
            <p class="par-lg">Also close to the University, SAIT, downtown, and the Foothills and new Children’s Hospital Hillhurst homes for sale and West Hillhurst real estate are well located and beautiful.</p>
