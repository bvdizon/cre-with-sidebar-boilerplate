    <h2 class="sect-title">About Renfrew</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">With the Trans-Canada Highway flanking it on the northern edge, the Renfrew community in North East Calgary lies north of the Bridgeland inner city community.</p>
            <p class="par-lg">Location is the key when it comes to the major popularity of homes for sale in Renfrew.</p>
            <p class="par-lg">There are a variety of housing options in the neighborhood ranging from single family detached houses and the more recently developed new infills that can be purchased for as cheap as $300,000 and as expensive as $1.5 million.</p>
