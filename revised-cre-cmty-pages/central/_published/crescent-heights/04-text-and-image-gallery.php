    <h2 class="sect-title">Community Vibe in Crescent Heights</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">The Crescent Heights Community Association was founded in 1977 and operates a community
                hall, with a standard kitchen, a stage, and an indoor fireplace. </p>
            <p class="par-xl">The City of Calgary has tennis courts, an outdoor skating rink, lawn bowling and a wading
                pool in the community, all available for public use.</p>

            <p class="par-lg">Running right through the middle of the community is the commercial area of Centre Street,
                which happens to the dividing line between the northeast and northwest. Centre Street is home to a large
                number of owner-run stores and restaurants.</p>
            <p class="par-lg">Across the Bow River in Prince’s Island, host of the Calgary Folk Festival and Shakespeare
                in the Park. Further south is Eau Claire Market, a small shopping centre famous for its owner-run
                establishments and home to a Cineplex Odeon, showing the latest movie releases. </p>
            <p class="par-lg">Eau Claire is also host to Taste of Calgary, where all of Calgary’s famous restaurants set
                up vendors and offer free samples of the various specialty foods they have to offer.</p>

            <p>Crescent Heights has a public high school, with other schools in the nearby communities.</p>


