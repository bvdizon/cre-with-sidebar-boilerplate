    <h2 class="sect-title">About Crescent Heights</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Crescent Heights is mostly made up of apartment buildings and row houses, making up 69.4%
                of the market, with the remaining 30.6% being single-detached and attached homes. </p>
            <p class="par-xl">As of 2006, 54.7% of the market was being used for rental purposes.</p>

            <p class="par-lg">Crescent Heights is located in Ward 7 of the Calgary City Council. The estimated
                population is 6,082. The community currently has an area redevelopment plan in place.</p>

