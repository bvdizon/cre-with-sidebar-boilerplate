    <h2 class="sect-title">About Highwood</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Located within the north-west quadrant of Calgary, Highwood is a residential neighborhood
                that is surrounded by the McKnight Blvd on the north, 4 Street W on the east, the Nose Hill Park on the
                west and the 40 Avenue N on the south.</p>

            <p class="par-lg">The community in Highwood was established in 1954 and has been one of the most desirable
                neighborhoods for young couples, large families, and students as well. </p>
            <p class="par-lg">Homes for sale in Highwood primarily comprise single-family detached homes with a few more
                recently developed multifamily residences and apartment condominiums as well. </p>
            <p class="par-xl">The picturesque streets, well-manicured lawns, panoramic views of the Rockies, tranquil
                environment and abundant recreational opportunities are a few of the several aspects of this beautiful
                community that lures homebuyers and investors alike.</p>

