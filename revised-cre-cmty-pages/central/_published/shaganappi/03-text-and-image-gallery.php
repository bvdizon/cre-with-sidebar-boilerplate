    <h2 class="sect-title">About Shaganappi </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">As one of the oldest residential communities of Calgary, Shaganappi is one of the most
                sought-after and exclusive places to live in Calgary. </p>
            <p class="par-xl">With many years of history at its core, Shaganappi has houses that hark to older days with
                their stylish architecture and designs.</p>
            <p class="par-lg">Besides, Shaganappi sits on a ridge overlooking the Bow River Valley, offering spectacular
                views to residents. </p>
            <p class="par-lg">Shaganappi is well-connected to the main areas of Calgary thanks its well-networked roads
                and routes.</p>

