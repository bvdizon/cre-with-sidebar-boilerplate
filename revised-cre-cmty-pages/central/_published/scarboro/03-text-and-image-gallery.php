    <h2 class="sect-title">About Scarboro</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">One of the more luxurious communities of Calgary, Scarboro is a charming residential
                locations perched on a Plateau, offering residents beautiful scenes and views. </p>
            <p class="par-xl">Scarboro overlooks downtown Calgary, and is one of the most picturesque communities of the
                regions, what with its winding roads and houses expertly perched to match the artistic charm of the
                place.</p>
            <p class="par-lg">Scarboro is a settlement made up of very few homes – something that has preserved its
                reputation of being one of the most sought-after, elite communities to have a home in. most of the
                houses are single-family homes, and make for perfect investments.</p>
            <p class="par-lg">Homes at Scarboro cost an average of $1,400,000. Homes are almost always surrounded by
                trees and large spaces, making it a very scenic place to live in. Scarboro’s proximity to downtown
                Calgary is an added advantage to the community.</p>
            <p class="par-lg">Scarboro is located and represented by Ward 8 on the Calgary City Council. As of December
                2012, Scarboro is now serviced by Sunalta station and Shaganappi Point station on the West C-train line,
                providing easy access to downtown for regular commuters.</p>

