<div id="community-vibe">
    <h2 class="sect-title">Community Vibe in Scarboro</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-xl">The Scarboro Community Association has administered the neighbourhood since 1934 and
                operates an outdoor skating rink and a sports/playing field. </p>
            <p class="par-lg">In the adjacent Sunalta community is Royal Sunalta Park, home to the Calgary Tennis Club,
                which operates public tennis courts. Bordering the community to the south is 17th Avenue, one of
                Calgary’s most popular shopping and entertainment districts. </p>
            <p class="par-lg">Uptown 17th is full of shops and restaurants, with the guarantee for someone to find a
                good time from the early morning to the late hours of the night. Uptown 17th has an abundance of retail
                stores, from the larger American Apparel to the owner operated Blame Betty and Dick & Jane’s. </p>
            <p class="par-lg">Uptown 17th has restaurants appealing to all needs, from the quick eats of Tubby Dog and
                Khm Anh Vietnamese Subs, to dine-in restaurants, from Fiore Cantina Italiana to World Bier Haus 1410.
                Nightlife is also easy to find; with the abundance of pubs, bars a nightclubs, one can quickly find a
                good night out.</p>

            <p class="par-lg">Scarboro has two public schools in the area operated by the CBC.</p>


