    <h2 class="sect-title">About Elboya </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Elboya is mainly comprised of detached homes, bungalow style, single detached homes and
                two storey homes, but also offers condominiums or apartments (making up 37.4% of buildings in Elboya).
            </p>
            <p class="par-xl">Avenue Magazine ranked Elboya as the #1 best neighbourhood in Calgary based on a community
                wide survey conducted in 2010.</p>

            <p class="par-lg">Elboya is home to one public elementary/junior high school and one Catholic school, with
                other schools in adjacent neighbourhoods.</p>

            <p class="par-lg">Medical clinics are located close by in adjacent communities, from dental offices to
                family doctors.
            </p>

