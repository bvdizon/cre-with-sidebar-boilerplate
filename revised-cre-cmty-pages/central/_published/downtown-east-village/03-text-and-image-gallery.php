    <h2 class="sect-title">About Downtown East Village</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Colloquially called simply as the East Village, the Downtown East Village is both a
                residential and
                commercial neighborhood located along the eastern edge of the city Downtown of Calgary. </p>
            <p class="par-lg">The community comprises the famous Fort Calgary that facilitated the earliest settlement
                in the entire
                city of Calgary.</p>

            <p class="par-lg">Located within the perimeters of Calgaryâ€™s River District, the community in Downtown East
                Village has
                undergone a tremendous transformation over the years. </p>
            <p class="par-xl">A great urban community for young families, professionals, and students alike, the
                Downtown East Village
                real estate offers an eclectic mix of both the early century historical buildings and new age
                contemporary architecture. </p>
            <p class="par-lg">Homes for sale in Downtown East Village are available within a price range of $300,000 to
                $970,000 and
                primarily comprise apartment condominiums. </p>
            <p class="par-lg">There are a number of recreational venues such as the New Central Library, National Music
                Centre of
                Canada and so on that are located within the neighborhood itself.</p>


