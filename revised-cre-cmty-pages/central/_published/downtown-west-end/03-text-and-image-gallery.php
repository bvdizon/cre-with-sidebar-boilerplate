    <h2 class="sect-title">About Downtown West End </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Located within the western region of Downtown Calgary, the Downtown West End is an urban
                residential neighborhood surrounded by the Bow River on the north, the 9th Street W on the east, the
                14th Street W on the west and the CPR tracks on the south.</p>
            <p class="par-lg">Once the Canadian Pacific Railway arrived in the area in 1883, the establishment of the
                community in Downtown West End was started in full swing. </p>
            <p class="par-xl">One of the most sought after locations in the entire city.</p>
            <p class="par-lg">The Downtown West End Real Estate majorly comprises apartment buildings and condominiums,
                with only around 25 single-family detached homes still remaining within the community.</p>
            <p class="par-lg">From paved multi-use pathways to the Mewata Armouries and the Shaw Millennium Park, there
                is a whole lot of entertainment in store for the potential homeowners in the area.</p>
            <p class="par-lg">Homes for sale in Downtown West End are readily available from $300,000 up to $1.25
                million for a single unit.</p>

