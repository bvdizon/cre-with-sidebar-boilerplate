    <h2 class="sect-title">Community Vibe in Cliff Bungalow </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Given its convenient location with respect to the CPR (Canadian Pacific Railway) rail
                yards and headquarters, the community in Cliff Bungalow is majorly populated by the CPR employees and
                their families. </p>
            <p class="par-lg">Cliff bungalow real estate offers you an eclectic mix of housing options ranging from the
                vintage turn of the century bungalows to the contemporary styled modern condominiums that are typically
                listed at an average price of around $1,006,800. </p>
            <p class="par-xl">Residents in the neighborhood can spend their leisure hours reveling in the myriad of
                recreational venues including the Elbow River Pathways and the Lindsay Park.</p>

