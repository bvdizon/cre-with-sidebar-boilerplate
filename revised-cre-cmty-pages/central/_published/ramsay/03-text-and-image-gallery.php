    <h2 class="sect-title">About Ramsay </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">An inner city community located within the south east quadrant of Calgary, </p>
            <p class="par-xl">Ramsay is a popular residential neighborhood that borders the Alyth-Bonny Brook industrial
                area on the south east. </p>
            <p class="par-lg">To the west of the community lies the Scotiabank Saddledome arena, the Stampede Grounds,
                the Macleod Trail and the Elbow River.</p>
            <p class="par-lg">Ramsay homes are mostly single-family detached homes, bungalow style and two storey homes.
            </p>
            <p class="par-lg">Currently, the community has an area redevelopment plan to replace older homes with newer
                ones. 31% of the homes in Ramsay are condominiums and apartments, with 46.9% of homes being used for
                rental purposes.</p>

