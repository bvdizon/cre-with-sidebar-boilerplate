    <h2 class="sect-title">About Garrison Woods </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">The community in Garrison Woods is one among the few recently developed neighborhoods that
                have quickly spiraled to popularity among young professionals, growing families and students alike.</p>
            <p class="par-lg">Renowned for its immaculate greens, tranquil environment, winding roads and beautiful
                treed paths. </p>
            <p class="par-xl">Garrison Woods Real Estate offers a leisurely paced lifestyle for people who like to take
                it easy. </p>
            <p class="par-lg">Homes for sale in Garrison Woods include single-family detached homes and apartment condos
                regularly listed at an average price of $635,000.</p>

