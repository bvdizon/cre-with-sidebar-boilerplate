    <h2 class="sect-title">Community Vibe in Chinatown </h2>
    <div>
        <p class="par-lg">When it comes to Chinatown, the location is most well-known for its traditional Chinese food and beverage
            options. Explore the bubble tea shops here and learn why this beverage has become quite so popular world
            over. The Try Again Beverages House is one of the must-go places in Chinatown for this purpose. With its
            amazing selection of unique flavours to choose from, you will have a gala time here exploring this new
            taste. This was one of the first bubble tea shops to come up in Calgary.</p>

        <p class="par-lg">Some unique Chinese dishes await you here as well. Silver Inn, where the very famous ginger beef is
            supposed to have first been introduced, is located right here in Centre street. Another great place for
            authentic Chinese is the Silver Dragon, which happens to be one of the oldest restaurants in Chinatown.
            Quite a few celebs visit this restaurant mainly for its Hong Kong-style dim sum.</p>

        <p class="par-lg">A rocking experience awaits you at Chinatown on Canada Day, when the whole community comes alive. They
            even shut down a part of the Riverfront Avenue so that food trucks can be set up there to cater to the
            people thronging the streets. This is a great time for you to experience authentic Asian fare as well as
            typical Albertan food well within budget.</p>

        <p class="par-lg">Dragon City Mall is a hotspot for locals who head out here when they need to get all of their chores done
            at one place. The mall has a good number of stores spread across three spacious levels.</p>

        <p class="par-lg">For the culture lovers, the Chinese Cultural Centre is a good spot to head to. The traditional
            architecture is the first thing that grabs attention, but this centre has much more to offer too. There
            are martial arts courses, Chinese cultural dance lessons and language classes for those who wish to
            learn Chinese or Mandarin. The Cultural Centre Chinese Learning Academy is located here too. Overall,
            this is a place where you can learn all about Chinese culture and heritage!</p>

