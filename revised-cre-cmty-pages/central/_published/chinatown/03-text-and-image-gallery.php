    <h2 class="sect-title">About Chinatown </h2>
    <div>
        <p class="par-lg">Flanking the Center Street in the north eastern part of the Calgary Downtown, the Chinatown is a rich
            cultural hub that offers a multi-ethnic community complete with modern conveniences for both first time
            home buyers and investors.</p>
        <p class="par-lg">It is no exaggeration to say that China town has served as the social centre point for the East Asian
            community in these parts. Intermixing of cultures from every part of the Asian world allows Chinatown
            residents to stay inclusive without giving up the rich heritage of their past.</p>
        <p class="par-lg">Highly adaptable as a community, Chinatown shows its mettle in many ways. For example, the tweaking of
            traditional Chinese recipes to match the Calgary lifestyle and tastes has resulted in unique fusion
            foods emerging in their quadrant of Calgary.</p>
