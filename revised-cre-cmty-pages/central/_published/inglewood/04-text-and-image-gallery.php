<div id="community-vibe">
    <h2 class="sect-title">Community Vibe in Inglewood</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">The Inglewood Community Association administers the community, and operates several tennis
                courts, an indoor swimming pool, an outdoor hockey and skating rink and a sports/playing field. The
                association also hosts programs, such as sports, jazzercise, yoga, zumba, dance classes and childcare.
                The community also has a Community Hall, with a kitchen, meeting rooms, a stage, sound system and
                outdoor BBQ.</p>

            <p class="par-lg">Inglewood has a business revitalization zone located along Ninth Ave (formerly Atlantic
                Ave.) with over 100 shops, boutiques and eateries, drawing locals and tourists. </p>
            <p class="par-lg">Music and theatre is a huge part of the Inglewood identity, with the Ironwood Stage and
                Grill hosting music and theatre productions weekly. </p>
            <p class="par-lg">Inglewood is also home to the Calgary International Fringe Festival, the International
                Festival of Animated Objects and much more. </p>
            <p class="par-xl">In the north boundary of Inglewood is the Bow River Pathway, a popular spot for cycling,
                jogging and hiking among other activities.</p>


