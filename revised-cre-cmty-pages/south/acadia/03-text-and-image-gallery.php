    <h2 class="sect-title">About Acadia</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">The community of Acadia was established in 1960. According to the 2012 consensus, the
                population of the
                Acadia was 10, 705 and the number of homes were recorded at 5,053. </p>
            <p class="par-lg">The development of the annexed land in Acadia began in 1960. Today, the community has a
                mix of residential, recreational and land uses. </p>
            <p class="par-xl">Even though Acadia is exposed to plenty of retail bustle, it continues to be a peaceful
                and friendly neighborhood
                and loved by both large and small families.</p>
            <p class="par-lg">Acadia real estate comprises a variety of different homes including
                single family homes, multi-family homes and condominiums.</p>

            <p class="par-lg">To the west of Acadia is Macleod Trail and it is eastbound by the Bow River. Fairmount
                Drive is the main
                street of the neighborhood and also the hub of all business activity.</p>


