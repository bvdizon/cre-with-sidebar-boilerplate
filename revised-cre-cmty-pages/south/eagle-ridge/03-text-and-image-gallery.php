    <h2 class="sect-title">About Eagle Ridge</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Situated in the southwest quadrant of Calgary, Alberta, Eagle Ridge is flanked by the
                Elbow River to the east and the Heritage Park Historical Village to the south. 14 street SW runs along
                the west of Eagle Ridge. </p>
            <p class="par-xl">The Glenmore Trail forms the northern border of this beautiful community. </p>
            <p class="par-lg">The average asking price for homes here is $1,500,000. On average the price of properties
                in this community range from $995,000 to $2,000,000.</p>
