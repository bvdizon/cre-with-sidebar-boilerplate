<div id="community-vibe">
    <h2 class="sect-title">Community Vibe in Bridlewood </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-xl">The Somerset-Bridlewood Community Association administers Bridlewood, with the Resident’s
                Association maintaining a water park, Volleyball courts, and tennis courts, all for public use. </p>
            <p class="par-lg">A five-minute drive away is Shawnessy’s shopping district, complete with restaurants, pubs
                and shopping boutiques. </p>
            <p class="par-lg">People can find various familiar fast-foods for quick, on-the-go meals, as well as a
                selection of dine-in restaurants, including a Moxies, Ricky’s All Day Grill and Zeller’s Restaurant.
                Hudson’s Bar and Grill is a mixture of a pub and nightclub, with the main floor being converted into a
                dance floor during late nights on weekends. </p>
            <p class="par-lg">Clothes shopping is the dominate boutique, with a Winners, Sports Check, and Warehouse one
                Jeans, among others. </p>
            <p class="par-lg">Bishop O’Byrne High School is a unique building, as it is a High School, YMCA and public
                library built into one, offering recreation, education and a quiet reading place for all ages.</p>

            <p class="par-lg">Bridlewood has a public elementary school, a Catholic school, and a private Christian
                school.</p>


