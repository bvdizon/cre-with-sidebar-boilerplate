    <h2 class="sect-title">About Deer Run</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">The community in Deer Run was established in 1978 and has undergone massive development
                over the years.</p>
            <p class="par-lg">Homes for sale in Deer Run are typically available at a starting rate of around $200,000
                with the prices climbing higher up to $600,000 for larger and more refined properties. </p>
            <p class="par-xl">A family-oriented community.</p>
            <p class="par-lg">The Deer Run Real Estate has everything from townhomes to single family detached units,
                starter homes and even apartment condos to suit the preferences of different homebuyers and investors.
            </p>
