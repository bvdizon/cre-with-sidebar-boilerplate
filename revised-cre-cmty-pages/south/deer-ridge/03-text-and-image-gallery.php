    <h2 class="sect-title">About Deer Ridge</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Developed in the late seventies, the community in Deer Ridge is represented by the Ward 14
                Councilor in the Calgary City Council. </p>
            <p class="par-xl">A great urban lifestyle with idyllic natural surroundings.</p>
            <p class="par-lg">The Deer Ridge Real Estate has a lot to offer to the average homebuyer and real estate
                investors looking for a lucrative deal for an affordable price. </p>
            <p class="par-lg">Homes for sale in Deer Ridge combine urban convenience with suburban lifestyle and include
                single-family detached homes and townhouses that are regularly listed for sale at an average price of
                around mid 500s. </p>
