    <h2 class="sect-title">About Chinook Park </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">The community in Chinook Park was developed in 1959 and houses some really artistic
                residential properties designed for the mid-century architecture. </p>
            <p class="par-lg">The charming little neighborhood is nestled between a number of other SW suburban
                communities including the Eagle Ridge, the Bayview, the Haysboro and the Kingsland. </p>
            <p class="par-lg">Homes for sale in Chinook Park include stand-alone homes, single family detached units and
                a few recently developed apartment condos as well. </p>
            <p class="par-xl">Chinook Park Real Estate is famous for its great quality of living at an affordable price
                tag.</p>
            <p class="par-lg">It ranges from as low as $200,000 to as high as a million for the premium high-end
                properties. </p>
