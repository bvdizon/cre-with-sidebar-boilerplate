    <h2 class="sect-title">Community Vibe in Kelvin Grove</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">With the famous Rockyview General Hospital located to its west, </p>
            <p class="par-xl">the Kelvin Grove is one of the most popular residential neighbourhoods in the Southwestern
                region of Calgary. </p>
            <p class="par-lg">The community is bound by 14 Street W on the west, the 75 avenue S on the south, the Elbow
                Drive on the east and the Glenmore Trail on the north.</p>
            <p class="par-lg">With its large open spaces and numerous walking trails, Kelvin Grove is an ideal community
                for outdoor enthusiasts.</p>
