    <h2 class="sect-title">About Kelvin Grove</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Established as a neighborhood in 1957, the community in Kelvin Grove is represented by the
                Ward 11 councilor in the Calgary City Council. </p>
            <p class="par-xl">The Kelvin Grove Real estate is a warm and welcoming residential area in this part of the
                town and is perfect for raising families with kids. </p>
            <p class="par-lg">Homes for sale in Kelvin Grove comprise mid-priced single family homes to luxury
                condominiums as well. You can purchase a typical residential unit in the neighborhood at a starting
                price of around $200,000 with some of the high end properties being marked at above $800,000.</p>
