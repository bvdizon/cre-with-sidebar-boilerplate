    <h2 class="sect-title">Community Vibe in Fairview </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Featuring the rich architectural character of the fifties and the sixties, Fairview is a
                charming residential neighborhood in South East Calgary. </p>
            <p class="par-lg">The community is bounded by the Macleod Trail on the west, the Heritage Drive on the
                south, the Blackfoot Trail on the east and the 66 Avenue S on the north.</p>
            <p class="par-lg">Many Fairview homes have a view of the Rocky Mountains. </p>
            <p class="par-xl">Fairview homes for sale enjoy larger than average yards and shopping conveniences. </p>
            <p class="par-lg">Near Glenmore and Deerfoot, home-owners have convenient access to transit and amenities.
            </p>
