    <h2 class="sect-title">About Fairview </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-xl">Annexed to Calgary in the late fifties, the community in Fairview is a busy and thriving
                neighborhood with a long and glorious history to boast of. </p>
            <p class="par-lg">Fairview Real Estate majorly comprises of mid-century bungalows and townhouses that were
                built on massive lots in the 50s and 60s and later renovated for the purpose of aligning them to the
                needs of the contemporary homebuyer. Most of the homes are still occupied by their original owners. </p>
            <p class="par-lg">Homes for sale in Fairview are typically built in a 1000sq ft area and are available for a
                starting price of around $300,000, with the more luxurious properties being priced at around $600,000.
            </p>
            <p class="par-lg">Condominiums and apartments make up 18.9% of the housing in Fairview, with 27.9% of
                Fairview homes being used for rental purposes. </p>
            <p class="par-lg">A majority of the homes in Fairview are residential detached homes, bungalow style, and
                two storey homes.</p>
