    <h2 class="sect-title">Community Vibe in Deer Ridge</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Nestled within the Bow River Valley is a quaint residential neighborhood called Deer Ridge
                that is one of the most sought-after communities in the entire South East Calgary. </p>
            <p class="par-lg">The community is bordered by the Bow Bottom Trail on the west, the Deerfield Circle on the
                south, the Deercliff Road on the east and the Canyon Meadows Drive on the north.</p>
            <p class="par-lg">The Fish Creek Provincial Park located immediately east of the community.</p>
            <p class="par-xl">There are abundant opportunities for the residents to indulge in recreational pursuits.
            </p>



