    <h2 class="sect-title">Community Vibe in Bayview</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-xl">The location of the Bayview real estate makes it one of the most sought after. </p>
            <p class="par-lg">Located in the southwest quadrant of Calgary, Alberta, Bayview is an upscale residential
                community. </p>
            <p class="par-lg">The waterfront community is on the peninsula south of Glenmore Reservoir. </p>
            <p class="par-lg">To the east of the Bayview real estate is the sprawling Glenmore Park while the 14th
                street SW is to its east. </p>
            <p class="par-lg">The 90th Avenue SW runs along the southern border of the Bayview community.</p>


