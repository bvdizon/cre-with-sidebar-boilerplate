    <h2 class="sect-title">About Woodlands </h2>
    <div class="col-grid-2">
        <div>

            <p class="par-lg">The community in Woodlands was established in 1976 and has undergone major developments
                over the last few decades. </p>
            <p class="par-xl">One of the most sought after communities within this part of the town, the Woodlands Real
                Estate is an ideal place to raise a family with kids or spend a quiet and fulfilling life post
                retirement. </p>
            <p class="par-lg">Homes for sale in Woodlands include single family homes, townhouses and apartment
                condominiums that are typically sold at a starting price of around mid 400s to as high as the mid
                eighties. </p>
            <p class="par-lg">With the Canyon Meadows Golf Course located entirely located within its boundaries, the
                woodlands community offers abundant recreational opportunities for its residents.</p>

