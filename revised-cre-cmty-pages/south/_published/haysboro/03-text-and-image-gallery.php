    <h2 class="sect-title">About Haysboro </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Plant your roots in a Haysboro home. Quoted as the neighborhood where no one leaves,
                Haysboro is an inner-city Calgary subdivision in Southwest Calgary. </p>
            <p class="par-xl">Quaint little bungalows, mature trees lining the streets and a small town charm. </p>
            <p class="par-lg">It’s not difficult to speculate on the reason Haysboro homes don’t often come up for sale.
            </p>
            <p class="par-lg">Haysboro is home to two public elementary schools and a junior high, as well as several
                Catholic and private schools.</p>
            <p class="par-lg">Haysboro is located in Ward 11 of the Calgary City Council. Haysboro has an estimated
                population of nearly 6000.</p>

