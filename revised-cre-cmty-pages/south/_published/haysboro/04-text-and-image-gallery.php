    <h2 class="sect-title">Community Vibe in Haysboro </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">The Haysboro Community Association is very active in the community and has a community
                hall complete with wheelchair access, a standard kitchen, lounge, game room, meeting rooms and sound
                system. The association works closely with the City of Calgary to operate outdoor tennis courts, an
                outdoor skating rink/hockey rink. The association also offers fitness, dance, youth justice and seniors
                programs.</p>
            <p class="par-xl">Haysboro has no shortage of activities, from shopping to outdoor recreation to even a
                travel back in time to Calgary’s roots. </p>
            <p class="par-lg">Two of Calgary’s major shopping centres are located a short drive to both the north and
                south, Chinook Centre and Southcentre Mall respectively; each mall is home to 200 – 250 stores, ranging
                from clothes to toys to home decor. </p>
            <p class="par-lg">Haysboro is within walking distance of South Glenmore Park along the Glenmore Reservoir
                and is a popular destination for hiking, jogging and biking/rollerblading; the park is guaranteed to
                provide amazing sights year round. Also overlooking Glenmore Reservoir is Heritage Park Historical
                Village, a recreation of the architecture in Calgary between the 1880’s up to the 1950’s. </p>
            <p class="par-lg">The park has authentic buildings from around Alberta dating to each era, fully restored in
                order to present visitors with a full history of not just Calgary, but the entire province.</p>


