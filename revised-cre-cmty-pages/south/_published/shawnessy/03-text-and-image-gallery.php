    <h2 class="sect-title">About Shawnessy </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">A popular choice of homebuyers and real estate investors in the South West corridor along
                Calgary’s southern border, Shawnessy is a charming suburban residential neighborhood in the city’s prime
                location.</p>
            <p class="par-lg">Developed in 1981, the community in Shawnessy is a rapidly expanding real estate hub for
                young couple, growing families and retirees alike. </p>
            <p class="par-lg">With the Fish Creek Provincial Park immediately north of it and the Shawnessy Town Center
                located on the east.</p>
            <p class="par-xl" The Shawnessy Real Estate offers abundant recreational opportunities for the residents.
                </p>
            <p class="par-lg">Homes for sale in Shawnessy typically comprise single family detached units, townhomes and
                apartments that can be yours for a price ranging from the mid 200s to the mid 500s.</p>


