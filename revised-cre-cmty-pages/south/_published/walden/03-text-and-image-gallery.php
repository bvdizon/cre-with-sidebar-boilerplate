    <h2 class="sect-title">About Walden </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Looking for a relaxed and ‘green’ lifestyle with a host of modern conveniences at your
                doorstep? </p>
            <p class="par-xl">Well, then the Walden community is the ideal residential neighborhood for you to settle
                down in the south-east quadrant of Calgary in Alberta.</p>
            <p class="par-lg">Established by the real estate giant Genstar, the community in Walden was approved by the
                Calgary City Council in 2007, with the first leg of development commencing in early 2008. </p>
            <p class="par-lg">Sprawled across an impressive 620 acres of land, the Walden Real estate was named after
                Henry David Thoreau’s famous book called Walden.</p>

