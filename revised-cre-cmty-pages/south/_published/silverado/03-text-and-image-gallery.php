    <h2 class="sect-title">About Silverado </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Located within the south-west quadrant of Calgary, Silverado is a tranquil residential
                community bound by the Spruce Meadows Trail on the north and the Spruce Meadows equestrian sports
                facility on the west.</p>
            <p class="par-lg">Established in 2005, the community in Silverado is one of the recently developed
                neighborhoods that attract buyers from all walks of life. </p>
            <p class="par-xl">Designed to embrace outdoor living, the Silverado Real Estate offers its resident abundant
                green spaces to enjoy a sense of community and savor the peace and tranquility of Mother Nature. </p>
            <p class="par-lg">Homes for sale in Silverado majorly comprise of large plots that house everything from
                starter homes to townhomes and even luxury estates. </p>
            <p class="par-lg">You can purchase at a residential property in the community at a starting price of around
                late 200s going up as high as a million and a half.</p>



