    <h2 class="sect-title">About Evergreen </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Evergreen is separated into two sections, the older, established homes being built in the
                1980's and the newer homes being constructed in 1999 or later, each section having their own separate
                Community Association and street naming patterns. </p>
            <p class="par-lg">Located on the south border of Fish Creek Provincial Park,</p>
            <p class="par-xl">Evergreen is within walking distance of beautiful vegetation and outdoor recreation.</p>
            <p class="par-lg">Evergreen is located in Ward 13 and represented by Diane Colley-Urquhart on the Calgary
                City Council. </p>
            <p class="par-lg">Provincially, the community lies in the Calgary-Lougheed riding and represented by Dave
                Rodney and in the federal riding of Calgary Southwest and represented by Stephen Harper, current Prime
                Minister of Canada. </p>
            <p class="par-lg">Evergreen is serviced by the Shawnessy LRT Station on the Somerset-Bridlewood/Dalhousie
                line.</p>
