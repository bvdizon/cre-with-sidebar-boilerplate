<div id="community-vibe">
    <h2 class="sect-title">Community Vibe in Deer Run</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">A charming residential neighborhood in the south east quadrant of Calgary.</p>
            <p class="par-lg">Deer Run is surrounded by a lovely park and landscaped with plentiful open green spaces.
            </p>
            <p class="par-lg">Set in a bend of the Bow River, Deer Run is a small community that offers a great quality
                of life within a congenial atmosphere.</p>
            <p class="par-xl">Recreational outdoor activities abound in this neighborhood, making it one of the most
                desirable communities for young families and retirees to settle down.</p>

