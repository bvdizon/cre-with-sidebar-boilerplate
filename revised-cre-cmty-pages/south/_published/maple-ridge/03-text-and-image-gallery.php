    <h2 class="sect-title">About Maple Ridge</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-xl">A family-friendly neighbourhood. Maple Ridge is a fantastic place to raise a family. </p>
            <p class="par-lg">The big Alberta sky flaunts her beauty over the lush green grass and trees of the
                community. </p>
            <p class="par-lg">Maple Ridge is a mixed residential and recreational community, with easy access to
                schools, entertainment and recreation.</p>
            <p class="par-lg">Maple Ridge is home to a public elementary and middle school, with a public high school in
                neighbouring Acadia. There is also a private French immersion school in Willow Park.</p>
