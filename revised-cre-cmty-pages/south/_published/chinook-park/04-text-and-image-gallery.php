    <h2 class="sect-title">Community Vibe in Chinook Park </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">A small and cozy residential community located in the South West quadrant of Calgary.</p>
            <p class="par-lg">Chinook Park is bounded by the Heritage Drive on the south, 14 Street W on the west, the
                Elbow Drive on the east and the Glenmore Trail on the north.</p>
            <p class="par-xl">Residents can enjoy close proximity to a number of shopping complexes, schools and public
                transportation facilities that abound in the community.</p>


