<div id="community-vibe">
    <h2 class="sect-title">Community Vibe in Acadia</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Acadia is located conveniently close to shopping centres, such as Deerfoot Meadows to the
                east, and is
                also
                in between SouthCentre Mall and Chinook Centre. Southland and Heritage LRT stations are located across
                McLeod Trail and provide train services to Downtown and Somerset/Bridlewood. LaserQuest is a family
                oriented
                laser tag arena located just across McLeod Trail.</p>
            <p class="par-xl">Acadia is a peaceful community located in the southeastern part of Calgary in Alberta.
            </p>
            <p class="par-lg">Acadia has three established public schools, an elementary, middle and senior high school,
                along with one
                private school and one Catholic school.</p>

            <p class="par-lg">Many recreational activities are available at the Acadia Recreational Complex (ARC),
                including Hockey,
                curling and basketball.</p>


