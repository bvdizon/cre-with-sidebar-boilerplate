    <h2 class="sect-title">About Legacy </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Nestled between the breathtaking Bow River Valley and the Macleod Trail SE along the
                southern boundary of the city.</p>
            <p class="par-xl">Legacy is a charming residential neighborhood located in South East Calgary. </p>
            <p class="par-lg">The community lies south of the community of Walden and houses the Hamlet of Heritage
                Pointe in the MD of Foothills No.31.</p>
            <p class="par-lg">Homes for sale in Legacy comprise townhomes, single family detached units and apartment
                condos that can be yours for a starting price of around $200,000, with the high-end properties available
                for as high as mid 800s.</p>

