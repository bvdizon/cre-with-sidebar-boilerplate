    <h2 class="sect-title">About Palliser </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-xl">Famous for the Tom Brook Athletic Park located within its premises, the Palliser community
                is a charming residential neighborhood in the South West quadrant of Calgary in Alberta. </p>
            <p class="par-lg">The community is bound by 24 Street SW on the west, the Southland Drive on the south, 19
                Street SW on the east and the 90 Ave SW on the north.</p>
            <p class="par-lg">The community in Palliser got its name from an early geographer and explorer from Western
                Canada called John Palliser. </p>
            <p class="par-lg">Homes for sale in Palliser are readily available at a starting price of around $300,000
                and go up to $900,000 for the premium proprieties. </p>
