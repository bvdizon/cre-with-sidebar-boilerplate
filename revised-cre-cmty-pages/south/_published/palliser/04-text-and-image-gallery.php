    <h2 class="sect-title">Community Vibe in Palliser </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">The roots of Palliser Real Estate were laid in the 1960s when the very first buildings in
                the community came into being.</p>
            <p class="par-xl">Located immediately south of the Glenmore Reservoir, the Palliser neighborhood offers a
                range of housing options from detached houses to townhomes and even apartments. </p>
            <p class="par-lg">There is a number of shopping complexes such as the Chinook Centre and the Southcenter
                Mall within close vicinity of Palliser that cater to the everyday needs of the residents.</p>

