    <h2 class="sect-title">Community Vibe in Midnapore </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">A residential community located within the city of Calgary, Midnapore is bounded by the
                Macleod Trail on the west, the Sun Valley Boulevard on the south, the Fish Creek Provincial Park on the
                east and north. </p>
            <p class="par-xl">A lake community in South Calgary, Midnapore is ideal for young couples, growing families
                and retirees alike.</p>
            <p class="par-lg">Residents can enjoy a bunch of recreational pursuits from the Mid-Sun Community Centre,
                the Midnapore Mall to the Fish Creek Provincial Park and a myriad of fast food restaurants located
                within the community.</p>
