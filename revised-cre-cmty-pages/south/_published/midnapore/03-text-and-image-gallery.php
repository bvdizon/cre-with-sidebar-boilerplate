    <h2 class="sect-title">About Midnapore </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Originally a small town that was annexed to Calgary in 1961, the community in Midnapore
                has undergone major development in the last couple of decades. </p>
            <p class="par-lg">Home to two of the most famous historical churches in the whole of Calgary. </p>
            <p class="par-xl">Midnapore Real Estate promises a great quality of living at a highly affordable pricing.
            </p>
            <p class="par-lg">Homes for sale in Midnapore include both single-family detached units as well as low-rise
                apartment condominiums that are regularly listed at a starting price of around $200,000, with the prices
                climbing as high as $900,000 for the luxury estate homes. </p>
        </div>
