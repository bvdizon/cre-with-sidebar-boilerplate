    <h2 class="sect-title">Community Vibe in Lake Bonavista</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Lake Bonavista is located directly north of Fish Creek Provincial Park, a section known as
                Glennfield. Three group use areas are located at Glennfield, which can be booked for family or other
                group BBQ’s. Surrounding these areas are firepits for hot-dog roasts and picnics. Fish Creek is also
                covered with pathways and hiking trails and is an excellent recreational and nature area, with a variety
                of wildlife and plant life. </p>
            <p class="par-lg">Lake Bonavista homes have two lakes nearby, the main one being Lake Bonavista, the second
                being Lake Bonaventure, a private use lake. Lake Bonavista residents receive a membership card and are
                welcome to use the Lake as they please. The lake is a pleasant place for recreational activity, whether
                it is playing tennis in the courts, swimming in the water or boating around the lake.</p>
            <p class="par-xl">Entertainment is not hard to come by. </p>
            <p class="par-lg">With the Southcentre Mall being across Anderson Road. Avenida Shopping Centre is located
                along the length of Lake Bonavista, with a limited number of retail stores ranging from fashion to food.
                Avenida Art Gallery is a major attraction in the shopping center, harboring a number of artworks and
                styles of different artists. Canyon Meadows Cinema shows new movie releases at cheap prices, making it a
                popular destination for moviegoers. Fun N’ More is a child-themed entertainment center, with indoor play
                structures, arcade, party rooms, and more. For dining, Boston Pizza is located in the Avenida Shopping
                Centre, with both a family lounge and a bar.</p>

            <p class="par-lg">Lake Bonavista has three public elementary schools and a junior high, as well as a
                Catholic elementary and junior high.</p>


