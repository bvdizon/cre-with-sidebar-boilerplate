    <h2 class="sect-title">About Millrise </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Millrise is located in Ward 13 and represented by Diane Colley-Urquhart on the Calgary
                City Council. </p>
            <p class="par-lg">Provincially, Millrise is in the Calgary-Lougheed riding, represented by Dave Rodney and
                federally in the Calgary Southwest riding, the current seat of Prime Minister Stephen Harper. </p>
            <p class="par-lg">The Fish Creek-Lacombe LRT station services Millrise and goes as far north as , passing
                along popular business areas and commercial districts.</p>
            <p class="par-xl">Millrise is administered by the Millrise Community association, which operates a hockey
                rink and an outdoor skating rink. </p>

