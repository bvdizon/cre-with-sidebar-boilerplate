    <h2 class="sect-title">About Woodbine </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Flanking the western edge of the city, Woodbine is a charming residential neighborhood in
                the south-west quadrant of Calgary. </p>
            <p class="par-lg">The community is bounded by the 24th Street West on the east, the Anderson Road on the
                north, the Fish Creek Provincial Park on the south and the Tsuu Tâ€™ina first nation.</p>
            <p class="par-lg">Established in 1980, the community in Woodbine is represented by the Ward 13 councilor in
                the Calgary City Council. </p>
            <p class="par-xl">With the Fish Creek Park sharing its northern boundary with it, the Woodbine Real Estate
                offers plenty of recreational opportunities for the residents. </p>
            <p class="par-lg">Homes for sale in Woodbine primarily comprise single-family detached homes that are
                regularly listed at a starting price of around $250,000, with the luxury properties being marked as high
                as up to two million dollars. </p>
            <p class="par-lg">Woodbine also boasts of having the lowest crime rate in the entire city of Calgary.</p>

