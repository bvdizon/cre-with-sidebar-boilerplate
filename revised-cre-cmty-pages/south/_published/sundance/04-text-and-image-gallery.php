    <h2 class="sect-title">Community Vibe in Sundance </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-xl">In the heart of the community is Lake Sundance, access available for all residents, who
                can bring friends and family for a lovely day in the sand and water, or take advantage of using the
                playground, tennis courts or the green grass. </p>
            <p class="par-lg">Bordering the community from the east is Fish Creek Provincial Park, a beautiful, scenic
                park full of lush vegetation and wildlife. Fish Creek is also home to the public Sicome Lake, where
                people are free to play and swim.</p>
            <p class="par-lg">On the other side of Macleod Trail is the Shawnessy shopping district, offering a varied
                selection of retail shops, from electronics to clothing and accessories. The area also has a number of
                restaurants. </p>
            <p class="par-lg">Empire Theatres has a cinema in the district, showing the latest releases and hit movies.
            </p>
            <p class="par-lg">Bishop Oâ€™Byrne High School is unique in the fact it is also home to a YMCA and a Calgary
                Public Library, both of which are open to the public.</p>
            <p class="par-lg">Sundance is home to the South Calgary Health Centre and the South Calgary Medical Clinic
                for easy access to medical need in the chance something happens.</p>

