    <h2 class="sect-title">About Sundance </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Sundance is part of the Mid-Sun Community Association (short for Midnapore-Sundance),
                which has a community hall complete with a gym, standard kitchen, a lounge and meeting rooms. The
                association offers sports programs, fitness programs, dance classes, senior programs and childcare. </p>
            <p class="par-xl">Excellent places for hiking, jogging and biking/rollerblading, Fish Creek is a popular
                destination to escape the urban areas and find peace.</p>
            <p class="par-lg">Sundance is home to a public elementary, junior high and high school.</p>

