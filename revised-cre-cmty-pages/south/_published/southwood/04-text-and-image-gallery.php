    <h2 class="sect-title">Community Vibe in Southwood </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Primarily a single family residential neighborhood, the community in Southwood was
                established back in 1959.</p>
            <p class="par-lg">Since a majority of the mid century bungalows have been here for near about half a
                century, a series of renovations have been made to keep them aligned with the demands of the
                contemporary homebuyer and investors. </p>
            <p class="par-xl">Homes for sale in Southwood comprise of single family detached and attached units with a
                few recently developed apartment condominiums. </p>
