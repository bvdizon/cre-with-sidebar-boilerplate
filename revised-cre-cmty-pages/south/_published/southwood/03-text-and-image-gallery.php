    <h2 class="sect-title">About Southwood </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-xl">Nestled within the south west quadrant of Calgary, Southwood is quaint residential
                neighborhood that promises a great quality of life for its residents.</p>
            <p class="par-lg">The community is bounded by the Anderson Road on the south, the 14th Street SW on the
                west, the Macleod Trail on the east and the 98th Avenue on the north.</p>
            <p class="par-lg">Property prices in Southwood Real Estate are pretty affordable and range from somewhere
                along the lines of $200,000 to as high as $600,000.</p>

