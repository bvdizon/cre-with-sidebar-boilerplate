    <h2 class="sect-title">Community Vibe in Oakridge </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Originally a part of the Municipal District of Rocky View, the community in Oakridge was
                annexed to the city of Calgary in 1956. </p>
            <p class="par-xl">Established in 1968, the Oakridge Real Estate offers an eclectic mix of housing options
                including single family detached units and townhomes that are surrounded by beautiful tree-lined streets
                and walking trails. </p>
            <p class="par-lg">While The Oakridge Co-op Shopping Plaza offers adequate amenities to cater to your
                everyday needs, the South Center Mall takes care of your more expensive shopping requirements.</p>

