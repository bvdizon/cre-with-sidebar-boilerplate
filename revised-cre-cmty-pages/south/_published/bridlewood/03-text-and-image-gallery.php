    <h2 class="sect-title">About Bridlewood </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Established in 1998, Bridlewood is a new neighbourhood compared to most other
                neighbourhoods in Calgary. </p>
            <p class="par-lg">Built with open space and next to a wetland. </p>
            <p class="par-xl">Bridlewood is lush, green, and full of wildlife; a great community for those with a family
                and for those who are active and love the outdoors. </p>
            <p class="par-lg">Bridlewood is located in Ward 13 and represented by Diane Colley-Urquhart on the Calgary
                City Council. </p>
            <p class="par-lg">Provincially, Bridlewood is in the Calgary-Lougheed riding, represented by Dave Rodney and
                federally in the Calgary Southwest riding, the current seat of Prime Minister Stephen Harper. The
                Somerset-Bridlewood LRT station services Bridlewood and is the last stop on the Dalhousie line.</p>



