    <h2 class="sect-title">About Chaparral </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">The community in Chaparral was established in 1995 and has undergone major development
                over the last two decades.</p>
            <p class="par-lg">Nestled along the southern edge of the city of Calgary, Chaparral is a residential
                neighborhood named after the famous Mediterranean climate Biome. </p>
            <p class="par-xl">One of the most desirable neighborhoods in the South east quadrant of the city.</p>
            <p class="par-lg">Chaparral is surrounded by the Bow River Valley on the east, the Macleod trail on the
                west, the Stoney Trail on the south and the 194 Avenue SE on the south.</p>


