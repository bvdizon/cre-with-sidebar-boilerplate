    <h2 class="sect-title">About Braeside</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Located within the south-west quadrant of Calgary.</p>
            <p class="par-xl">Braeside is a tranquil residential neighborhood that has a great deal to offer both
                homebuyers as well as
                real estate investors. </p>
            <p class="par-lg">The community is bordered by the 24th Street SW on the west, the 14th Street SW on the
                east, the
                Southland Drive on the north and the Anderson Road on the south.</p>
            <p class="par-lg">Typically, all the housing units in Braeside average somewhere on the lines of $400,000.
            </p>



