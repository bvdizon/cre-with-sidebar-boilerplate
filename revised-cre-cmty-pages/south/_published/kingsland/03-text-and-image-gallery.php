    <h2 class="sect-title">About Kingsland </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-xl">Established in 1957, Kingsland is a residential community nestled within the south-west
                quadrant of Calgary
                in Alberta.</p>
            <p class="par-lg">The bungalows built in the early fifties have undergone multiple renovations to upgrade
                them according to
                the needs and expectations of the contemporary home buyer and real estate investors.</p>
            <p class="par-lg">Homes for sale in Kingsland are readily available within a price range from the mid 300s
                to as high as 900s.</p>

