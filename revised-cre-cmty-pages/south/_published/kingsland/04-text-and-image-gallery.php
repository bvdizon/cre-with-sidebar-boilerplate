<div id="community-vibe">
    <h2 class="sect-title">Community Vibe in Kingsland </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">A quiet yet well-urbanized neighborhood.</p>
            <p class="par-lg">The community in Kingsland combines the old world charm of the mid-century architecture
                and the modern age starter homes and apartment condominiums.</p>
            <p class="par-lg">The neighborhood is surrounded by the Elbow Drive on the west, the Heritage Drive on the
                south, the Macleod Trail on the east and the Glenmore Trail on the north.</p>
            <p class="par-xl">Residents can enjoy a plethora of recreational venues and activities that are provided by
                the community association.</p>

