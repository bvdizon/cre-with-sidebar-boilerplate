    <h2 class="sect-title">Community Vibe in Queensland </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Queensland is administered by the Queensland Community Association, which operates tennis
                courts, outdoor skating rink, and a sports/playing field as well as programs including Bingo (at Jackpot
                Junction), sports, fitness, youth justice, senior programs and childcare (preschool). The community hall
                has wheelchair access, a gym, standard kitchen, meeting rooms, stage, and outdoor BBQ. </p>
            <p class="par-lg">Fish Creek is also popular for its picnic areas along the Bow River, as well as the
                wildlife and vegetation.</p>
            <p class="par-xl">In nearby Deer Valley, Deer Valley Shopping Centre has a home, incorporating both an
                indoor and outdoor area. </p>
            <p class="par-lg">Currently, the Deer Valley Mall is undergoing reservations to add new shops and acres to
                the building. Deer Valley Mall has a selection of owner-operated shops, while outside has familiar
                places, such as Co-op, Sobeyâ€™s, Starbucks and Dairy Queen. There is also the Winking Owl Pub, a popular
                Irish pub.</p>

            <p class="par-lg">The CBE operates two elementary schools and one junior high.</p>



