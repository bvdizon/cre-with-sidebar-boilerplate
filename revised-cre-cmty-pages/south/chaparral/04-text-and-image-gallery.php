    <h2 class="sect-title">Community Vibe in Chaparral </h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Set around a 32 acre artificial lake and a 21 acre park with two waterfalls as the
                centerpieces.</p>
            <p class="par-xl">The Chaparral Real Estate is one of the few neighborhoods in Calgary that boast of
                extraordinary natural beauty and landscaping.</p>
            <p class="par-lg">The homes for sale in Chaparral are clustered around the lake and park and comprise
                majorly of stylish townhomes, detached houses and luxury estates. </p>
            <p class="par-lg">The plethora of walking oaths, tennis courts, baseball diamonds and playgrounds facilitate
                an engaging lifestyle for both parents and their kids.</p>


