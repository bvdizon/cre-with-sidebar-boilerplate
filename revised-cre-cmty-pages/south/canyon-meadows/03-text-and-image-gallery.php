    <h2 class="sect-title">About Canyon Meadows</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Canyon Meadows is an idyllic residential neighborhood nestled within the south-west
                quadrant of Calgary. The community is surrounded by 14 Street W on the west, the Canyon Meadows Drive on
                the south, the MacLeod trail on the east and the Anderson Road on the east.</p>

            <p class="par-xl">Situated in southwest Calgary, Canyon Meadows features affordable and comfortable living
                options. </p>
            <p class="par-lg">To the east of the community is Lake Fraser Drive, while Macleod Trail SW runs along the
                western border. Canyon Meadows Drive is at the southern border of this community. Anderson Road marks
                the northern border of Canyon Meadows.</p>

            <p class="par-lg">Development of the Canyon Meadows community started and continued throughout the 1960s. At
                Canyon Meadows, homes for sale include a mixture of starter, luxury, and moderately priced condos. A
                wide range of options in terms of pricing is available starting from 1.9 million to $273k.</p>


