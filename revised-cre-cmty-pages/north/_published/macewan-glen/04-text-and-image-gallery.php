    <h2 class="sect-title">Community Vibe in MacEwan Glen</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">A marvelous suburban neighborhood located within the north west quadrant of Calgary. </p>
            <p class="par-lg">MacEwan Glen is bound by the Nose Hill on the south, the Shaganappi Trail on the west, 14 Street W on the east and the Country Hills Boulevard on the North. </p>
            <p class="par-lg">Additionally, there are multiple shopping malls such as the Beacon Hill, the Market Mall and the Beddington Town Centre located at a walkable distance from a majority of residential units within the neighborhood.</p>
