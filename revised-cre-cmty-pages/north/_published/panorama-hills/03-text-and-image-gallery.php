    <h2 class="sect-title">About Panorama Hills</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">A typical suburban neighborhood in the north-west region of Calgary. Panorama Hills is one of the more recently-developed communities in the city.</p>

            <p class="par-lg">The marvelous neighborhood lies to the north of the Country Hills Boulevard and was established in 1996.</p>


            <p class="par-lg">The Panorama Hills Real estate majorly comprises single-family detached homes that are regularly listed for sale at an average price of $450,000, with the costs climbing as high as $1,100,000 for the more luxurious bungalow units.</p>

