    <h2 class="sect-title">About Evanston</h2>
    <div class="col-grid-2">
        <div>
            <p class="par-lg">Established in 2002, Evanston is one of the most recently developed projects in the city and is bordered by the Hanson Ranch and <a href="/hidden-valley.php">Hidden Valley</a> communities to the north.</p>
            <p class="par-lg">A family oriented neighborhood located in North West, Calgary, Evanston is one of the five communities that constitute the Symons Valley area. </p>
            <p class="par-lg">A major chunk of Evanston homes for sale comprise of single family detached homes. </p>
