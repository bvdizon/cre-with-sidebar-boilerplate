<h2 id="more">Thorburn Neighbourhood</h2>
<p>Thorburn is an established community located in the southeast part of Airdrie, Alberta. The community is only a 10-minutes drive from Calgary. The area was developed in the early 1980s, with construction activities continuing for nearly three decades. Thorburn&acirc;&euro;&trade;s closeness to the East Lake and the surrounding green space make it one of the best communities for people coming to the city of Airdrie.</p>
<h2>Thorburn Real Estate</h2>
<p>The Thorburn real estate comprises single-family dwellings with a starting price in the range of early 400s and going up to mid 800s on progressing to larger homes. The Thorburn community has a number of amenities, including convenience stores, gas stations, and a shopping mall. Thorburn homes for sale can be found easily on regular offline and online real estate listings. It&acirc;&euro;&trade;s certainly one of the best communities to live in Airdrie.</p>
