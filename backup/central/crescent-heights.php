<h2 id="more">Crescent Heights Real Estate Market</h2>
<p>Crescent Heights is mostly made up of apartment buildings and row houses, making up 69.4% of the market, with the
    remaining 30.6% being single-detached and attached homes. &nbsp;As of 2006, 54.7% of the market was being used for
    rental purposes.</p>
<p>Crescent Heights is located in Ward 7 of the Calgary City Council. The estimated population is 6,082. The community
    currently has an area redevelopment plan in place.</p>
<h2>Things to do in and around Crescent Heights</h2>
<p>The Crescent Heights Community Association was founded in 1977 and operates a community hall, with a standard
    kitchen, a stage, and an indoor fireplace. The City of Calgary has tennis courts, an outdoor skating rink, lawn
    bowling and a wading pool in the community, all available for public use.</p>
<p>Running right through the middle of the community is the commercial area of Centre Street, which happens to the
    dividing line between the northeast and northwest. Centre Street is home to a large number of owner-run stores and
    restaurants. Across the Bow River in Prince&rsquo;s Island, host of the Calgary Folk Festival and Shakespeare in the
    Park. Further south is Eau Claire Market, a small shopping centre famous for its owner-run establishments and home
    to a Cineplex Odeon, showing the latest movie releases. Eau Claire is also host to Taste of Calgary, where all of
    Calgary&rsquo;s famous restaurants set up vendors and offer free samples of the various specialty foods they have to
    offer.</p>
<p>Crescent Heights has a public high school, with other schools in the nearby communities.</p>
