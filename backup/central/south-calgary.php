<h2 id="more">South Calgary Real Estate Market</h2>
<p>South Calgary&rsquo;s 50-year development is reflected through the variety of housing styles, with lowrise apartments
    and townhouses, to attached homes and detached bungalow&rsquo;s and two storey homes. The area, having a view of
    downtown and the mountains, also makes it ideal for luxury homes. In 2001, 61.1% of properties in South Calgary were
    used for rental purposes, but the percentage is likely to have changed.</p>
<p><span style="font-size: 20px;">South Calgary Homes</span></p>
<p>South Calgary real estate is known for its proximity to the trendy Marda Loop area. Situated between the subdivisions
    of <a href="/bankview.php">Bankview</a>, <a href="/altadore.php">Altadore</a> and <a
        href="/richmond.php">Richmond</a>, South Calgary homes are well maintained and this vibrant neighbourhood is
    re-creating itself with gorgeous new homes being developed for sale. A quick commute to downtown Calgary make homes
    for sale in South Calgary highly sought after.</p>
