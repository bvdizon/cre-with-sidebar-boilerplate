<h2 id="more">Richmond</h2>
<p>One of Calgary&rsquo;s oldest and popular established residential neighborhoods, Richmond is spread out on both sides of the Crowchild Trail in South West Calgary. Originally a location for the famous Alberta Children&rsquo;s Hospital, the community replaced the hospital facility after it was moved to NW Calgary in 2007.</p>
<h2>Richmond Real Estate</h2>
<p>Established in 1950, the community in Richmond is renowned for its eclectic mix of housing units that range from the post-war age bungalows to the modern apartment condominiums and infills. Since a number of properties date back several decades, there has been massive reconstruction projects to renovate the bungalows in order to align them with the needs and expectations of the contemporary homebuyer. Homes for sale in Richmond are readily available between a price range of $300,000 up to around a million as well. Located within the close vicinity of the community, Marda Loop offers endless recreational opportunities to the residents in the form of restaurants, cafes and shopping stores.</p>
