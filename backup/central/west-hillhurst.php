<h2 id="more">West Hillhurst</h2>
<p>Located in the inner suburbs of northwest quadrant of Calgary, Alberta, West Hillhurst is an executive residential community. The southern border of West Hillhurst is marked by the Bow River, while 9th Avenue NW is towards the northern border. The Crowchild runs through and bisects West Hillhurst community. The western border is flanked by 28 Street NW, while to the east is 18a Street NW.</p>
<h2>West Hillhurst Real Estate</h2>
<p>The origins of the development of West Hillhurst real estate can be traced back to 1945 when &ldquo;victory homes&rdquo; were built for soldiers of World War II in the neighborhood. West Hillhurst homes for sale have an average asking price of $970,000, with a percentage of homes being $2 million or more. Homes in West Hillhurst feature a delightfully eclectic mix of old and new.</p>
