<h2 id="more">Things to do in and around Rosedale</h2>
<p>Rosedale is a short distance away from the Kensington Business Revitalization Zone, home to a variety of owner run shops and well-known Calgary restaurants, including the Broken Plate, a famous Greek restaurant. The area is also home to the Plaza theatre, which hosts showings of classics, independent films and low-budget film festivals. Down 16th Ave west is North Hill Centre, a small mall with over 100 stores and 2 major anchors, a Sears and a Safeway. Rosedale is adjacent to ACAD and SAIT Polytechnic, where the nearest C-train station is located, providing quick and easy transport to the downtown core for daily commuters. Rosedale Community Centre is home to the Calgary Ski Club, offering ski trips to members, as well as skiing lessons for people new to the sport. The community also operates public tennis courts. Located in nearby Hillhurst is Riley Park and Burns Memorial Park, great places to enjoy the outdoors.</p>
<p>The CBE operates a elementary/junior high school in the area.</p>
<h2>Rosedale Misc.</h2>
<p>Rosedale is located near Foothills Hospital, with medical clinics including dentists and family doctors operating in nearby Kensington BRZ. C-train access is available via SAIT/ACAD/Jubilee station. Rosedale is located in Ward 7 of the Calgary City Council.</p>
