<h2 id="more">Downtown East Village</h2>
<p>Colloquially called simply as the East Village, the Downtown East Village is both a residential and commercial
    neighborhood located along the eastern edge of the city Downtown of Calgary. The community comprises the famous Fort
    Calgary that facilitated the earliest settlement in the entire city of Calgary.</p>
<h2>Downtown East Village Real Estate</h2>
<p>Located within the perimeters of Calgary&rsquo;s River District, the community in Downtown East Village has undergone
    a tremendous transformation over the years. A great urban community for young families, professionals, and students
    alike, the Downtown East Village real estate offers an eclectic mix of both the early century historical buildings
    and new age contemporary architecture. Homes for sale in Downtown East Village are available within a price range of
    $300,000 to $970,000 and primarily comprise apartment condominiums. There are a number of recreational venues such
    as the New Central Library, National Music Centre of Canada and so on that are located within the neighborhood
    itself.</p>
