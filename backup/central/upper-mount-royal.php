<h2 id="more">Upper Mount Royal</h2>
<p>Upper Mount Royal has always been the home to Calgary&rsquo;s elite. Dating back to more than a century, some of the
    houses here are over 100 years old! The houses are built in the typical classic red-brick style and make for a very
    picturesque location what with the scenic surroundings of Upper Mount Royal. The community has preserved its
    exclusivity as one of the most prime locations to have a home, and it remains one such to this day.</p>
<h2>Upper Mount Royal Real Estate</h2>
<p>The average price of a house in 2016 was $1,712,000. Almost all the houses are detached, single-family properties, as
    can be expected in a neighborhood such as Upper Mount Royal. When considering the purchase of property in Upper
    Mount Royal, be careful of getting confused with Lower Royal Mount, which is not as high-class as Upper Mount Royal.
    Naturally, the options on Upper Mount Royal are fewer, more expensive, but located at the choicest most convenient
    spots.</p>
<h2>Upper Mount Royal Homes</h2>
<p>Formerly American Hill, Upper Mount Royal homes began as an enclave of American Business elite. Upper Mount Royal
    features luxury real estate in one of the wealthiest neighbourhoods in Calgary and is situated overlooking Elbow
    Park. Lower Mount Royal is a more typical Calgary community consisting largely of lowrise condos and townhomes. One
    of Calgary&rsquo;s older neighbourhoods, many of the Upper Mount Royal homes are an image of Calgary&rsquo;s
    beginnings as a city, and where the men and women who made the city what it is today once lived.</p>
