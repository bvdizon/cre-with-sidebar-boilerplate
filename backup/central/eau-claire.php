<h2 id="more">Eau Claire</h2>
<p>Located immediately north of Downtown Calgary, is a lovely residential neighborhood of Eau Claire. The community was
    established on a reclaimed industrial land near the Bow River and lies immediately north of 3rd Avenue South.</p>
<h2>Eau Claire Real Estate</h2>
<p>The community in Eau Claire got its name from a sawmill transplanted from the area of Eau Claire in Wisconsin as one
    of the earliest industries in Calgary. Eau Clair Real Estate comprises an eclectic mix of housing options ranging
    from beautiful riverside condominiums to townhouses and so on. Some of the recreational venues in the area include
    the Prince&rsquo;s Island Park, Barclay Mall, Eau Claire&rsquo;s Market, hotels, pubs, restaurants and several
    offbeat cinemas. Homes for sale in Eau Claire are regularly available from somewhere around the mid 500s up to a
    million for the more luxurious properties.</p>
