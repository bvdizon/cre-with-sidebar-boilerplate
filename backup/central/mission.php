<h2 id="more">Mission Homes</h2>
<p>Originally established as a French Catholic Mission, Mission homes sit in the &nbsp;entertainment centre of Calgary.
    Although once known as the Village of Rouleauville, Mission real estate has fully assimilated into the Urban
    atmosphere, and still offers beautiful outdoor scenery along the Elbow River.&nbsp;</p>
<h2>Things to do in and around Mission</h2>
<p>Running right down the middle of Mission is 4th Street, home to a selection of restaurants, shopping and public
    services. Mission has become a popular area for restaurants, with over 35 on 4th street alone and a majority having
    patio space for summer use. Unique restaurants range from the more gourmet foods, such as Mercato International
    restaurant and market offering the best quality Italian food, to the more quick bites from 4th Street Pizza to
    Burger Inn. Nightlife on 4th is also popular, from the local Joyce on 4th Pub to Wurst, a Bavarian themed Beer Hall.
    17th Avenue is one of Calgary&rsquo;s most popular areas for entertainment and shopping. Nellies Cosmic Caf&eacute;
    is a popular breakfast and brunch destination for it&rsquo;s good food and friendly service. The night is booming
    with the assortment of pubs, bars and clubs along the length of 17th Avenue, including Republik Nightclub, host to a
    selection of DJ&rsquo;s every Friday with no cover charge.</p>
<p>Talisman Centre, formerly Lindsey Park, is located in Mission and is a popular multi-sports complex, with summer
    camps available beginning in July and events held year round for various different celebrations. Across McLeod Trail
    is the Calgary Stampede Grounds, home to the annual Calgary Stampede every July, which attracts over a million
    visitors from around the world and presents a great deal of entertainment, from music, to rodeo to performances to
    the most magnificent Canadian artwork.</p>
