<h2 id="more">Meadowlark Park Real Estate Market</h2>
<p>As of 2006, 96.1% of the Meadowlark real estate is made up of detached single-family homes (bungalow and two storey), with the remaining 3.9% being apartments. 17.3% of the homes are used for rental purposes.</p>
<h2>Things to do in and around Meadowlark Park</h2>
<p>Meadowlark Park Community association administers the community and operates a community centre, complete with standard kitchen and stage, with wheelchair access to the building. Chinook Centre is located east of the community and is Calgary&rsquo;s largest indoor shopping centre, with over 250 stores and Scotiabank Theatre complex. Anchored by The Bay, two new anchors, Nordstrom and Target, are to be added to replace Sears and Zellers respectively. Besides the food court in the mall, there is also McSorley&rsquo;s Pub and Chop Steakhouse are fancy restaurants located around the mall, with there also being a Red Lobster and Ricky&rsquo;s All Day Grill across the street.</p>
<h2>Meadowlark Park Misc.</h2>
<p>Meadowlark Park is located in Ward 11 and represented by Brian Pincott on the Calgary City Council. Provincially, Meadowlark Park is located in the Calgary-Elbow riding, represented by Alison Redford, current Premier of Alberta and federally in the Calgary Centre riding, currently without representation after the resignation of Lee Richardson.</p>
