<h2 id="more">Things to do in and around Mount Royal</h2>
<p>17th Avenue borders the north of the community and is a favourite destination for people looking for entertainment
    and shopping. A mix of well known retail shops and unique owner-operated shops, from Blame Betty to Purr, shoppers
    will find a satisfying amount of shops to browse around and buy different accessories and clothing. 17th Avenue also
    offers a large array of dining choices, from the quick bites of Tubby Dog and Kim Anh Vietnamese Subs to the
    restaurants of Fiore Cantina Italiana to Boston Pizza.</p>
<p>River Park and Altadore Park are located in the south end of Mount Royal and are an excellent outdoor area for
    picnics, fires and afternoon walks. The upper portion of River Park is an off-leash dog park, making it ideal for
    dog owners.</p>
<p>Mount Royal is home to a public elementary and junior high, with a high school on 17th Avenue.</p>
<h2>Mount Royal Misc.</h2>
<p>Mount Royal homes are located in close proximity to medical services, from dental to family doctors. Located in Ward
    8 of the Calgary City Council and Calgary-Currie provincial district, the area is currently represented by Alderman
    John Mar and MLA Christine Cusanelli respectively.</p>
