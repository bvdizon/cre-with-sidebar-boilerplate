<h2 id="more">Things to do in and around Windsor Park</h2>
<p>The Windsor Park Community Association administers the community and has a community center, complete with standard
    kitchen, meeting rooms and stage. The association also maintains an outdoor skating rink and sports/playing field
    and hosts yoga, dance and senior programs. Chinook Centre mall is located south-east of the community and is
    Calgary&rsquo;s largest enclosed shopping center, with over 250 stores, a football field-sized food court, and major
    theatre complex. With the closure of two major anchors, Zellers and Sears, Target and Nordstrom will respectively
    replace each as the new anchors. Windsor Park is home to the Calgary Golf and Country Club, an 18-hole golf course
    established in 1897, making it Alberta&rsquo;s oldest private golf and country club.</p>
<p>The CBE operates a public elementary school, with a child development center also located in the community.</p>
<h2>Windsor Park Misc.</h2>
<p>Windsor Park is located in Ward 11 and represented by Brian Pincott on the Calgary City Council. Provincially,
    Windsor Park is located in the Calgary-Elbow riding, represented by Alison Redford, current Premier of Alberta and
    federally in the Calgary Centre riding, currently without representation after the resignation of Lee Richardson.
</p>
