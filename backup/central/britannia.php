<h2 id="more">Britannia</h2>
<p>The southwest quadrant of Calgary houses the small but upscale Britannia community. Known for elegant architecture of
    its homes and beautiful landscapes, Britannia is located west of Elbow River and Elbow Drive. The Riverdale Park is
    to the west side of Britannia, while to the north is Britannia Drive.</p>
<h2>Britannia Real Estate</h2>
<p>Britannia became part of Calgary in 1910 and developed into a neighborhood in the 1950s. The upscale Britannia real
    estate features mostly single-family detached homes with condos and apartments forming a small part of the
    community. Britannia real estate offers a great combination of tranquility and convenient location that is popular
    among families looking for long-term housing. The average price of homes for sale in Britannia real estate is
    $2,995,000.00. Options range between $490,000 and $4,900,000.</p>
<h2>Britannia Real Estate Market</h2>
<p>The majority of property in Britannia are single-detached homes, making up 71% of the market, with the remaining 29%
    made up of apartments. In 2006, 27.5% of the homes were used for rental purposes, in par with the Calgary average. A
    smaller community, the area has a population of 746 as of 2011.</p>
<h2>Things to do in and around Britannia</h2>
<p>Britannia is bordered to the west by Riverdale Park, traveling along the Elbow River; the park has pedestrian bridge
    access to River Park, Sandy Beach Park, and Altadore Park, providing easy access to outdoor activities from picnics
    to jogging to simply taking in the fresh air. Elbow Drive provides quick access to downtown and to 4th Street, known
    for its abundance of caf&eacute;s, restaurants, and shops, making it one of the most popular destinations in
    Calgary. 4th Street in intersected by Uptown 17th business revitalization zone, and is several kilometers of shops,
    restaurants, clubs, and bars, making it an entertainment center for all times of the day. Whether it&rsquo;s the
    owner-run shops or the wild night out you prefer, there is bound to be something to catch your fancy.</p>
<p>There are currently no schools in Britannia, but schools K-12 are available in close proximately in the surrounding
    area.</p>
