<h2 id="more">Mount Pleasant</h2>
<p>A peaceful residential neighborhood located in the north-west quadrant of Calgary, Mount Pleasant lies immediately north of the community of <a href="/rosedale.php">Rosedale</a> and is bound by the Confederation Park on the west and north.</p>
<h2>Mount Pleasant Real Estate</h2>
<p>A breathtaking neighborhood in NW Calgary, the community in Mount Pleasant houses some of the most highly sought after residential properties in the entire city. The beautifully treed community offers a wide range of housing options including wartime bungalows, townhomes, apartment condos and new in-fill homes as well. Homes for sale in Mount Pleasant are typically listed at an affordable price of around $700,000 with the high-end properties available for as much as $1.5 million. The lovely 160 hectares crescent-shaped Confederation Park to the north of the community offers abundant recreational opportunities for the residents.</p>
