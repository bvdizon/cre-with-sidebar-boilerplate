<h2 id="more">Capitol Hill</h2>
<p>Sharing its southern border with the Trans-Canada Highway, the Capitol Hill is a beautiful residential neighborhood
    in the North West quadrant of Calgary. A quaint neighborhood, Capitol Hill is highly desirable among the young
    couples, growing families and retirees alike.</p>
<h2>Capitol Hill Real Estate</h2>
<p>Despite the fact that the community in Capitol Hill was established in the late forties, you will be able to spot a
    few old world buildings that have been there since 1910! Since the Capitol Hill Real Estate is located within the
    immediate vicinity of the University of Calgary and the Southern Alberta Institute of Technology, a majority of the
    residents comprise young students and teachers. Homes for sale in Capitol Hill include single family detached units
    and bungalows as well as apartment condominiums that are available at an average price of $690,000. With the famous
    Confederation park bordering the community on the north, the residents enjoy abundant recreational opportunities
    including running trails and tennis courts.</p>
