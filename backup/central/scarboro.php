<h2 id="more">Scarboro</h2>
<p>One of the more luxurious communities of Calgary, Scarboro is a charming residential locations perched on a Plateau,
    offering residents beautiful scenes and views. Scarboro overlooks downtown Calgary, and is one of the most
    picturesque communities of the regions, what with its winding roads and houses expertly perched to match the
    artistic charm of the place.</p>
<h2>Scarboro Real Estate</h2>
<p>Scarboro is a settlement made up of very few homes &ndash; something that has preserved its reputation of being one
    of the most sought-after, elite communities to have a home in. most of the houses are single-family homes, and make
    for perfect investments. Homes at Scarboro cost an average of $1,400,000. Homes are almost always surrounded by
    trees and large spaces, making it a very scenic place to live in. Scarboro&rsquo;s proximity to downtown Calgary is
    an added advantage to the community.</p>
<h2>Things to do in and around Scarboro</h2>
<p>The Scarboro Community Association has administered the neighbourhood since 1934 and operates an outdoor skating rink
    and a sports/playing field. In the adjacent Sunalta community is Royal Sunalta Park, home to the Calgary Tennis
    Club, which operates public tennis courts. Bordering the community to the south is 17th Avenue, one of
    Calgary&rsquo;s most popular shopping and entertainment districts. Uptown 17th is full of shops and restaurants,
    with the guarantee for someone to find a good time from the early morning to the late hours of the night. Uptown
    17th has an abundance of retail stores, from the larger American Apparel to the owner operated Blame Betty and Dick
    &amp; Jane&rsquo;s. Uptown 17th has restaurants appealing to all needs, from the quick eats of Tubby Dog and Khm Anh
    Vietnamese Subs, to dine-in restaurants, from Fiore Cantina Italiana to World Bier Haus 1410. Nightlife is also easy
    to find; with the abundance of pubs, bars a nightclubs, one can quickly find a good night out.</p>
<p>Scarboro has two public schools in the area operated by the CBC.</p>
<h2>Scarboro Misc.</h2>
<p>Scarboro is located and represented by Ward 8 on the Calgary City Council. As of December 2012, Scarboro is now
    serviced by Sunalta station and Shaganappi Point station on the West C-train line, providing easy access to downtown
    for regular commuters.</p>
