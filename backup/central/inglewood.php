<h2 id="more">Inglewood Real Estate Market</h2>
<p>As of 2006, 44.5% of Inglewood Homes is comprised of apartments and condominiums, with the remaining 44.7% being
    single-detached and attached homes (bungalows and two storey).</p>
<h2>Inglewood Real Estate</h2>
<p>Inglewood is Calgary&rsquo;s oldest area and it oozes old-world charm mixed with modern chic. Homes in Inglewood were
    once neighbours to second hand shops and purveyors of antiques.&nbsp; Inglewood real estate has a rich history and
    has now become neighbour to the newest fine restaurant and design district. Transformed by an influx of fine
    restaurants and trendy services Inglewood is a unique and complimentary variation of cheap to chic. Spend the day in
    Inglewood enjoying gorgeous scenery and relaxing on the riverfront. Visit&nbsp; some of Calgary&rsquo;s and
    Canada&rsquo;s top ranked restaurants such as Rouge. Miles of tree lined paths, spacious parks, the Bow River,
    playgrounds and sports fields. Inglewood is the perfect place for year round active living</p>
<h2>Things to do in and around Inglewood</h2>
<p>The Inglewood Community Association administers the community, and operates several tennis courts, an indoor swimming
    pool, an outdoor hockey and skating rink and a sports/playing field. The association also hosts programs, such as
    sports, jazzercise, yoga, zumba, dance classes and childcare. The community also has a Community Hall, with a
    kitchen, meeting rooms, a stage, sound system and outdoor BBQ.</p>
<p>Inglewood has a business revitalization zone located along Ninth Ave (formerly Atlantic Ave.) with over 100 shops,
    boutiques and eateries, drawing locals and tourists. Music and theatre is a huge part of the Inglewood identity,
    with the Ironwood Stage and Grill hosting music and theatre productions weekly. Inglewood is also home to the
    Calgary International Fringe Festival, the International Festival of Animated Objects and much more. In the north
    boundary of Inglewood is the Bow River Pathway, a popular spot for cycling, jogging and hiking among other
    activities.</p>
<h2>Inglewood Misc.</h2>
<p>Inglewood is located in Ward 9 and represented by Gian-Carlo Carra on the Calgary City Council. Provincially,
    Inglewood is in the Calgary-Fort riding, represented by Wayne Cao and federally in the Calgary-Southeast riding,
    represented by Jason Kenney.</p>
