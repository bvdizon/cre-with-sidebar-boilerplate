<h2 id="more">Things to do in and around Mayfair</h2>
<p>Mayfair is located close to Chinook Centre, Calgary&rsquo;s largest indoor shopping centre, with over 250 stores and boutiques, a food court approximately the size of a football field and a multiplex cinema showing the latest releases. Chinook has stores aimed at all genders and fashion preferences. Apart from the food court, there is also McSorley&rsquo;s Pub and Chop Steakhouse, as well as Ricky&rsquo;s All Day Grill and Red Lobster being located across McLeod Trail.</p>
<p>North of the community in <a href="/bel-aire.php">Bel-Aire</a> is the Calgary Golf and Country Club, Alberta&rsquo;s oldest private golf course, which has a magnificent view of the Glenmore Reservoir. Close by is North Glenmore Park and Weaselhead, excellent spots for jogging, hiking, picnics and bbq&rsquo;s.</p>
<p>Mayfair is home to a private school.</p>
<h2>Mayfair Misc.</h2>
<p>Mayfair is located in Ward 11 and represented by Brian Pincott on the Calgary City Council. Provincially, Mayfair is in the Calgary-Elbow riding, represented by Alberta Premier Alison Redford and federally in the Calgary Centre riding, with no representation following the resignation of Lee Richardson.</p>
