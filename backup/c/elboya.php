<h2 id="more">Elboya Real Estate Market</h2>
<p>Elboya is mainly comprised of detached homes, bungalow style, single detached homes and two storey homes, but also offers condominiums or apartments (making up 37.4% of buildings in Elboya). Avenue Magazine ranked Elboya as the #1 best neighbourhood in Calgary based on a community wide survey conducted in 2010.&nbsp;</p>
<p>Elboya is home to one public elementary/junior high school and one Catholic school, with other schools in adjacent neighbourhoods.</p>
<h2>Things to do in and around Elboya</h2>
<p>The Elbow River park and pathway system is an excellent place for joggers and walkers, full of beautiful scenery and birdlife, being a popular destination not just for the members of the adjacent communities, but also for all Calgarians. Stanley Park borders Elboya to the north. With Elbow Drive and McLeod trail being the major transportation arteries, downtown is only a minute away, making easy convergence for those who work and enjoy the nightlife of the city core. 4th Street in Mission and 17th Avenue also are in close proximity, both being popular entertainment and shopping destination for Calgarians. Restaurants in the area include Nellies Breakfast, Mercato Italian and Boston Pizza. The nightlife is booming with various bars and clubs, from The Ship &amp; Anchor to Repulbik, it doesn&rsquo;t take long to find somewhere to have a good night.</p>
