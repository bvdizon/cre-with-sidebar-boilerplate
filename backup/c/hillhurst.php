<h2 id="more">Hillhurst</h2>
<p>A vibrant and dynamic residential neighborhood located within the inner city of Calgary just north of the City Downtown and Bow River, Hillhurst is the perfect place to live, shop and play! With a glorious history dating back to the 1914, Hillhurst features a fascinating old world charm that is reflected in its architecture as well as landscaping.</p>
<h2>Hillhurst Real Estate</h2>
<p>With a population of around 5000, the community in Hillhurst is popular for its beautiful tree-lined streets, small well kept homes and paved pathways. Typically available at an average price of around $960,000, homes for sale in Hillhurst include a variety of budget options ranging from small starter homes for young couples and bachelors to palatial condominiums for the wealthy and influential. A number of recreational activities such as squash courts, gymnasiums, climbing walls and Olympic Ovals are provided by the nearby University of Calgary.</p>
<h2>Hillhurst Real Estate</h2>
<p>Hillhurst and West Hillhurst are Northwest Calgary&nbsp;inner city neighbourhoods,&nbsp;located on the Bow River beside the trendy shops and restaurants of Kensington. Original homes date back to 1907 when the community was first developed and new homes were built. The area is split into 2 sections; west of 19th street is known as West Hillhurst, and Hillhurst runs eastward to 14th street. Close to downtown Calgary&rsquo;s established subdivision, Hillhurst homes have been turned into inner-city in-fills. Also close to the University, SAIT, downtown, and the Foothills and new Children&rsquo;s Hospital&nbsp;Hillhurst homes for sale&nbsp;and&nbsp;West Hillhurst real estate&nbsp;are well located and beautiful.</p>
<h2>Things to do in and around Hillhurst</h2>
<p>Residing within Hillhurst is Kensington Business Revitalization Zone (BRZ). Whilst sharing the zone with Sunnyside, Kensington lies mostly in Hillhurst and has become a popular, trendy area, full of shops, restaurants and coffee/tea shops. Famous places include Broken Plate Greek restaurant, Kensington Pub, and Oolong Tea House. Kensington hosts Salsa Festival in July and Christmas in Kensington Village in December. Shopping in Kensington is varied, with art stores (fitting for its proximity to ACAD), unique clothing boutiques, and other accessories. Along with Memorial Drive is the Bow River walkway, extending down a majority of the Bow River as in encompasses the downtown core. The pathway leads to several Calgary attractions, including Eau Claire Market/Princes Island and The Calgary Zoo. The walkway is a great place for recreation and for quick walks to popular Calgary destinations.</p>
<p>Hillhurst has three public schools run by the CBE, a community school, and elementary and a junior high/high school. The Catholic School Board also has a fine arts elementary school. SAIT Polytechnic has a home bordering the north of Hillhurst, overlooking the beautiful Calgary skyline.</p>
<h2>Hillhurst Misc.</h2>
<p>Medical clinics are available around Kensington area, from dentistry to family doctors. Foothills Hospital is located just up the hill for major emergencies in the event one should happen. Hillhurst shares a close community with Sunnyside, as Kensington is divided between the two neighbourhoods. The nearest C-train station is Sunnyside station on the Somerset/Dalhousie line, making quick transport to almost all of Calgary&rsquo;s attractions.</p>
