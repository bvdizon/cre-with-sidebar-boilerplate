<h2 id="more">Sunnyside Homes</h2>
<p>The community of Sunnyside dates back to the first settlers in Calgary in the 1880&rsquo;s, and was incorporated into the city itself in 1904, making it a rather historical set of land. Once home to Canadian Pacific Railway and the Eau Clare Saw Mill workers, Sunnyside now attracts Urban Professionals and their families, who are drawn to the area for its quiet and friendly atmosphere located within walking distance to shops, restaurants, transportation and the downtown Calgary core.</p>
<h2>Things to do in and around Sunnyside</h2>
<p>Not only does the close proximity give residents much to do, Sunnyside is also located near Kensington, a trendy business and commercial district, focused at the intersection of 10th St. NW and Kensington Road. The area is home to various unique shops, including Kensington Art Supply (appropriate for the close proximity to SAIT and ACAD) Urban Barn and The Rocket, a clothing chain exclusive to Calgary. Food and entertainment is also available, from evening/nightlife places like Kensington Pub to more relaxed places like Oolong Tea House. A Safeway is located in north Kensington, making grocery shopping an easy trip for Sunnyside residents. Broken Plate is a popular Greek restaurant chain in Calgary with a restaurant in Kensington, with Fast-Forward Magazine praising its classic Greek dishes and stylish, comfortable setting, going as far to say &ldquo;doesn&rsquo;t need fixing.&rdquo;</p>
<p>Schools located in Sunnyside include one public school operated by the CBE.</p>
<h2>Sunnyside Misc.</h2>
<p>Medical services are not hard to come by, with Foothills Hospital just down Memorial Drive, and various clinics in the Kensington district. Sunnyside Station services the community with C-train access, making easy transport around the city.</p>
