<h2 id="more">Greenview</h2>
<p>With its rolling hill slopes, idyllic landscapes and high elevation, the Greenview community in North West Calgary offers some of the most brilliant views of the surrounding panorama. Initially a part of the Thorncliffe/Greenview neighborhood, the community got separated in 1991 and is now among the most desirable in the entire city.</p>
<h2>Real Estate</h2>
<p>Given its prime location with respect to ease of access to public transportation, schools, recreational venues and other amenities, the community in Greenview is popular among both homebuyers as well as real estate investors. Homes for sale in Greenview comprise an eclectic mix of housing options ranging from two-storey homes, to split level units, bungalows, townhouses, duplexes and even condominiums that are readily available at an average price of $250,000. The highly active community association of the neighborhood features a bunch of interesting leisure activities and also organizes various social events such as craft fairs and dances from time to time.</p>
