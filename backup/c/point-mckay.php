<h2 id="more">Point McKay</h2>
<p>Originally a part of the town of Montgomery, Point Mckay is a highly desirable residential neighborhood in the North West Calgary. The community is surrounded by the Bowness Road on the north and the Bow River on the south.</p>
<h2>Point McKay Real Estate</h2>
<p>Nestled along the banks of the Bow River, the community in Point McKay offers a great quality of life within a tranquil setting of immaculate greens, idyllic walking and cycling trails and breathtaking views of the river valley. Homes for sale in Point McKay comprise an eclectic mix of housing options including high rise apartment complexes, condos and townhomes that are typically listed within a price range of $200,000 and $600,000. With the nearby Riverside Club offering an entire range of activities including an adult fitness pool, a salt water pool and indoor tennis court, the residents have plentiful options to stay engaged.</p>
