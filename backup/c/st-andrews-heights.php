<h2 id="more">Things to do in and around St. Andrews Heights</h2>
<p>St. Andrews Heights Community Association operates a community hall, complete with a standard kitchen, wheelchair access, and meeting rooms. The City of Calgary operates tennis courts, a soccer field, playing field and a baseball diamond. The association also offers Sports, fitness and seniors programs. North of the community is the Norma Bush and Father David Bauer Arenas.</p>
<p>St. Andrews Heights is a short distance from Kensington Business Revitalization Zone, home to famous restaurants include Broken Plate Greek restaurant, Kensington Pub, and Oolong Tea House. Kensington hosts Salsa Festival in July and Christmas in Kensington Village in December. Shopping in Kensington is varied, with art stores (fitting for its proximity to ACAD), unique clothing boutiques, and other accessories. Kensington is also home to the Plaza Theatre, known for its showings of independent films, cult classics, and locally made films.</p>
<h2>St. Andrews Heights Misc.</h2>
<p>St. Andrews Heights is located in Ward 7 of the Calgary City Council. The estimated population is 1,470.</p>
