                <h2>ABOUT BOWNESS REAL ESTATE</h2>
                <h3>Bowness &mdash; Its History</h3>
                <p>Bowness' life as a residential community began with its sale to property developer John Hextall in
                    1908. Hextall proceeded to build a bridge, organize streetcar access, and subdivide the existing
                    property into a collection of lots he called Bowness Estates.</p>
                <p>The community grew slowly at first, but by 1964 it was large enough to be incorporated into the city
                    of Calgary. Today, Bowness consists of the area south of Bow River, and north of the Trans-Canada
                    Highway, extending westward until Stony Trail.</p>
                <h4>Explore Bowness Real Estate</h4>
                <p>Real estate in Bowness is a diverse mix of luxury homes along the shores of the Bow River, and
                    various new-traditional and Mid-Century single-family homes further from the water.</p>
                <p>First time buyers, or those who need a more compact space, can choose from a number of condos and
                    semi-detached homes found throughout Bowness. Throughout the area, mature trees line the spacious
                    front-lawns, which remain mostly unobstructed thanks to presence of rear alleys for convenient
                    access and storage.</p>
