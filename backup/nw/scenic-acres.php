<h2 id="more">Scenic Acres</h2>
<p>A warm and welcoming neighborhood; Scenic Acres definitely lives up to its name and boasts of some of the most idyllic views in the city, with downtown Calgary to the South East and the peaks of the Canadian Rockies in Banff National Park lying to the West. Located at a distance of around 14km from the Calgary CBD, Scenic Acres lies between the Crowchild Trail (Highway 1A) and Bow River in the North West.</p>
<h2>Scenic Acres Real Estate</h2>
