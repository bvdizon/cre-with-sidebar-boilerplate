<h2 id="more">University Heights Real Estate</h2>
<p>A well knit community within close proximity of the University of Calgary, the University Heights homes boast of a prime location that has become a major housing hot spot for many real estate buyers and sellers in NW Calgary area.</p>
<p>Built in the year 1963, the community in University Heights has also garnered considerable attention given the fact that the erstwhile Prime Minister Stephen Harper stayed in the neighborhood during his University days.</p>
<p>The University Heights homes for sale comprise of a variety of dwellings such as townhouses, condominiums and single family homes of different sizes and styles. Many homes have undergone considerable remodeling and renovation over the years.</p>
<p>Apart from the university, the University Heights real estate also enjoys close proximity to the Foothills Medical Centre, the McMahon Stadium, the Foothills Athletic Park and the Children&rsquo;s Hospital. The fact that the community lies in a posh and active neighborhood, renders the average asking-price for real estate above the Calgary real estate average.</p>
<p>There are a variety of public and private Schools in University Heights including Banff Trail, University Heights Elementary School, Branton, William Aberhart, Queen Elizabeth Junior High School, Senator Patrick Burns and many more.</p>
