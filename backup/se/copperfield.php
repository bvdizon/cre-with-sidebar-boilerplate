<h2 id="more">Copperfield Real Estate Market</h2>
<p>Copperfield&rsquo;s real estate is mostly single detached and attached homes, making up 86.6% of the market, with the remaining 13.4% being townhouses. As of 2006, 3.7% of homes were being used for rental purposes.</p>
<h2>Things to do in and around Copperfield</h2>
<p>Copperfield is part of the Marquis de Lorne Community Association, which also encompasses the community of <a href="/mahogany.php">Mahogany</a>. Annual memberships are available for residents, which provides discounts to community business partners, 15% off Family Membership to the Key Partner Family Leisure Centre, and much more. In the north of Copperfield Park is the community hall, complete with wheelchair access, meeting rooms, a sound system and an indoor fireplace.</p>
<p>North of Copperfield is <a href="/mckenzie-towne.php">McKenzie Towne</a>, which has a small commercial district, characterizing a typical Main Street one would find in any small town. Located on High Street, the area has a pharmacy, restaurants, cafes, grocery store and a pub, bringing many needs closer to home. For those looking for more retail, you&rsquo;ll find what you&rsquo;re looking for along 130th avenue. The street is lined with restaurants, big box stores, pharmacies, and various other retail and miscellaneous shops for people to explore.</p>
<h2>Copperfield Misc.</h2>
<p>Copperfield is located in close proximity to the South Calgary hospital, and there are family doctors and dental clinics in the surrounding communities.</p>
