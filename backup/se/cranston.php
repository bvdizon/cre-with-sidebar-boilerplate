<h2 id="more">Cranston</h2>
<p>A master planned community located in the south-east quadrant of Calgary in Alberta, Cranston is bounded by the Deerfoot Trail on the east, the Bow River on the south, the Fish Creek Park on the west and the Stoney Trail on the north.</p>
<h2>Cranston Real Estate</h2>
<p>Home to some of the most beautiful suburban homes set amidst natural surroundings, the community in Cranston can be rightly touted as every nature lover&rsquo;s dream come true. With a glorious history of around 15 years, the Cranston Real Estate has matured beautifully over the years and is one of the most popular and thriving neighborhoods in this part of the town. Homes for sale in Cranston comprise a mix of single-family and multifamily residential units that enjoy spectacular views of the Bow River Valley and the surrounding Rockies. You can choose from the variety of housing options that are available within a price range of around $500,000 to as high as a million and a half.</p>
<h2>Cranston Real Estate Market</h2>
<p>Cranston&rsquo;s homes are mostly single-detached and attached homes, making up a massive 99.3% of the real estate, with the remainder being a mixture of apartments and townhouses. 2.3% of the housing in Cranston is being used for rental purposes. As of 2011, Cranston has an estimated population of 10, 381.</p>
