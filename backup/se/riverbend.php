<h2 id="more">Riverbend</h2>
<p>A charming residential neighborhood in the south-east quadrant of Calgary, Riverbend is bounded by Deerfoot Trail on the west, 24 Street E on the east, Glenmore Trail on the north. The community has been developed within the Bow River floodplain.</p>
<h2>Riverbend Real Estate</h2>
<p>Established in 1982, the community in Riverbend is represented by the Ward 9 Councilor in the Calgary City council. A highly vibrant community, the Riverbend real estate enjoys a prime location in the heart of the city and offers a lot in terms of recreation for its residents. Homes for sale in Riverbend average some along the lines of mid 500s.</p>
