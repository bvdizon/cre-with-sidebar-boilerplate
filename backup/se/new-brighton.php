<h2 id="more">Things to do in and around New Brighton</h2>
<p>The New Brighton Community maintains a community hall, with wheelchair access, meeting rooms and a kitchen. In cooperation with the City of Calgary, the association also offers recreation facilities, including tennis courts, an outdoor hockey and skating rink, a soccer and baseball field and an outdoor water park. The association offers various community programs, including tot and youth soccer, a running club and a Jelly Bean dance. The community is also an active Block Watch neighbourhood. In the neighbouring McKenzie Town community is a small commercial district, mimicking a typical Main Street one would find in any small town. Located on High Street, the area has a pharmacy, restaurants, cafes, grocery store, and a pub, bringing many needs closer to home. A larger commercial area is located north of McKenzie Towne along 130th avenue. The street is lined with restaurants, big box stores, pharmacies, and various other retail and miscellaneous shops for people to explore.</p>
<h2>New Brighton Misc.</h2>
<p>New Brighton is located in close proximity to the brand new South Alberta Hospital. On High Street and 130th Avenue, there are the doctor and dental clinics to provide easy access for families.</p>
