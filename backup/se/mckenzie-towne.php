<h2 id="more">McKenzie Towne</h2>
<p>McKenzie Towne homes have the nostalgic feeling of those in a small town and the community takes rigorous steps to ensure that feeling, including naming the community centre McKenzie Town Hall. The community is separated into four areas: Inverness, Elgin, Prestwick and High Street, each with different architecture which give off the feeling of European style. Luscious ponds and green spaces characterize the community as an eco-community.</p>
<h2>Things to do in and around McKenzie Towne</h2>
<p>McKenzie Towne shows a close relationship with McKenzie Lake, Mountain Park (in McKenzie Lake) and Copperfield. The McKenzie Town Hall is home to the McKenzie Towne Council (the area homeowners association) and also includes recreational facilities, such as a gymnasium, meeting and banquet facilities and a private outdoor park. Various pathways circumnavigate McKenzie Towne, making the community an ideal place to enjoy the outdoors High Street acts as McKenzie Townes downtown core, with an assortment of shops, including a Fish and Chip restaurant, Kilt &amp; Caber Ale House, Second Cup and Sobey&rsquo;s Groceries. Brewsters Brewing Company also has a pub located on McKenzie Towne Ave with a fantastic rooftop patio, just a block away. The McKenzie Towne Farmers Market is an annual event, occurring every Thursday from June until September.</p>
<p>130th Ave is a shopping centre in the north end of McKenzie Towne, with over 130 shops and services. A Safeway, Wal-Mart and Real Canadian Superstore are located within the centre, bringing easy grocery access to the surrounding communities. Pharmaceutical and health services are widely offered, with a London Drugs, Medicentres, MCI doctors offices and Amaranth Health and Wellness Centre. Family restaurants available are Montana&rsquo;s Cookhouse, Swiss Chalet, Boston Pizza, Kelsey&rsquo;s and Tony Roma&rsquo;s. For adults, several pubs and bars a re located in the centre, such as Woody&rsquo;s Taphouse, Muldoon&rsquo;s Irish Ale House and Original Joes.</p>
<p>Public and Catholic schools are located in adjacent communities McKenzie Lake and Mountain park.</p>
