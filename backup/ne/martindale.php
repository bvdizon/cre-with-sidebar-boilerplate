<h2 id="more">Martindale</h2>
<p>Martindale is a North East Calgary residential neighborhood that is represented by the Ward 5 Councilor in the Calgary City Council. The community is bounded by the 44 Street E on the west, the 64 Avenue N on the south, the Falconbridge Blvd on the east and the 80 Avenue N on the north.</p>
<h2>Martindale Real Estate</h2>
<p>Established in 1983, the community in Martindale consists of an eclectic mix of housing options ranging from single family homes to townhouses and even apartment condominiums that are readily available at an average price of around $390,000. Owing to its central location within the city, the Martindale Real Estate offers a variety of public amenities such as schools, parks, recreational venues and shopping complexes that are just a stone&rsquo;s throw away from a majority of the residential units within the community.</p>
