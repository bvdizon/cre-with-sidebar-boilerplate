<h2 id="more">Rundle</h2>
<p>A part of the four communities together referred to as &lsquo;The Properties&rsquo;, Rundle is a master-planned residential neighborhood in North East Calgary. The community is flanked by 36 Street E on the west, 52 Street E on the east, the Trans-Canada Highway on the south and the 32 Avenue N on the north.</p>
<h2>Rundle Real Estate</h2>
<p>Established in 1973, the community in Rundle was named after a 19th-century missionary called Robert Terrill Rundle who worked in Western Canada. Rundle Real Estate is one of the most sought-after communities in the city owing to the wide range of quality housing options that it offers. Homes for sale in Rundle include single-family attached and detached units along with bungalows and condominiums that are readily available at an average price of around $310,000. Residents can enjoy a plethora of recreational pursuits at the nearby Village Square Leisure Centre and shopping facilities at the Sunridge Mall that is both located a stone&rsquo;s throw away from the community.</p>
