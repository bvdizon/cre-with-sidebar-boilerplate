<h2 id="more">Marlborough</h2>
<p>A tranquil residential neighborhood in the north east quadrant of Calgary, Marlborough is one of the older communities in the city and is bound by the 36 Street E on the west, the Memorial Drive on the south, the 52 Street E on the east and the Trans-Canada Highway on the north.</p>
<h2>Marlborough Real Estate</h2>
<p>Established in the late sixties, the community in Marlborough is renowned for its old-world charm and is quite popular among contemporary homebuyers and investors alike. The neighborhood was named after the city of Marlborough in England and offers all the modern amenities and conveniences that one could ask for. A majority of the homes for sale in Marlborough comprise of single-family detached homes and bungalows with a few apartment condominiums that have been developed only recently. The average price of a typical suburban townhome in Marlborough is somewhere along the lines of $360,000. The residents enjoy easy access to a variety of shopping malls, libraries, playgrounds and schools in the neighborhood.</p>
