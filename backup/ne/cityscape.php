<h2 id="more">Cityscape</h2>
<p>A master-planned residential community located in the north east quadrant of Calgary, Alberta, Cityscape is bordered by the future Airport Trail NE on the south, future 60 Street NE on the east, Country Hills Boulevard NE on the south and the Metis Trail NE on the west.</p>
<h2>Cityscape Real Estate</h2>
<p>The major focal point of the community in Cityscape is a humungous environmental reserve that is sprawled across a staggering 115-acre and provides a perfect balance between urban and natural lifestyle. Homes for sale in Cityscape range from simple Village Homes and laned homes to urban townhomes and Front Drive detached residential units as well. Available at an average price of $480,000, the housing units in Cityscape real estate are popular for their visually appealing architectural designs with open plan layouts and recessed garages. Given the relatively low crime rate in the neighborhood, Cityscape is ideal for raising kids as well as spending a quiet and relaxing life post-retirement.</p>
