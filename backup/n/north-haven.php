<h2 id="more">North Haven</h2>
<p>A master-planned, family-oriented residential community located within the North West quadrant of Calgary, North Haven lies to the east of Calgary&rsquo;s largest municipal reserve, the Nose Hill Park. Established in the early sixties, the neighborhood is represented by Ward 4 Councilor in the Calgary City Council.</p>
<p>North Haven Real Estate</p>
<p>From lush green parks to scenic walking trails, tree-lined streets and a plethora of public amenities located at a stone&rsquo;s throw, the community in North Haven features everything that makes it a perfect settlement option for young couples, growing families and retirees alike. Homes for sale in North Haven primarily comprise of single-family detached homes often listed at an average price of $450,000, with a small selection of the more affordable townhomes as well. Since most of the bungalows were built nearly half a century ago, a majority of the housing units have undergone a series of renovations to match the changing needs of the contemporary homebuyer.</p>
