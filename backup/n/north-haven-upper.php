<h2 id="more">North Haven Upper</h2>
<p>Established back in 1977, the North Haven Upper is a part of the North Haven community and is separated from the Lower North Haven by a bus trap. This beautiful neighborhood offers some of the most spectacular views of the surrounding landscape and makes for a perfect option for residential settlement for both young families as well as retirees.</p>
<h2>North Haven Upper Real Estate</h2>
<p>Bordered on the west by one of Canada&rsquo;s largest municipal reserve, the Nose Hill Park, the community in the North Haven Upper is located at a convenient distance from a variety of public amenities and recreational venues. What with the presence of a high-profile golf course and the famous Calgary Winter Club, the North Haven Real estate a highly sought-after neighborhood in the entire city. Homes for sale in North Haven Upper comprise a wide selection of housing options ranging from 2-storey split level homes to luxurious bungalows and condominiums, with a typical residential unit listed at an average price of $460,000.</p>
