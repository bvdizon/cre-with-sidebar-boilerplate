<h2 id="more">Forest Heights</h2>
<p>A small but well-planned residential community located within the South-East Quadrant of Calgary, Forest Heights is flanked by the <a href="/forest-lawn.php">Forest Lawn</a> on the south. The community is also surrounded by the <a href="/penbrooke-meadows.php">Penbrooke Meadows</a> on the east, the Fonda on the west, the 17 Avenue on the south and the Memorial Drive on its northern boundary.</p>
<h2>Forest Heights Real Estate</h2>
<p>Offering some of the most breathtaking panoramic views of the City skyline and the majestic Canadian Rockies, the community in Forest Heights is a great choice for young couples, growing families with kids and even the elderly. Forest Heights Real Estate offers its future residents a variety of affordable housing options ranging from townhomes, single-family detached units as well as apartment condos that are typically available at an average price of $350,000. The community also features a variety of shopping malls and recreational venues that are located at a convenient walkable distance form a majority of the residential units within the neighborhood.</p>
