<h2 id="more">Ogden Real Estate Market</h2>
<p>In 2006, approximately 63.8% of the Ogden real estate was made up of detached and semi-detached single-family homes (duplex up and down, triplex, bungalow and two storey), with the remaining 36.2% being apartments and condominiums. Approximately 35.2% of homes were being used for rental purposes.</p>
<h2>Things to do in and around Ogden/Lynnwood</h2>
<p>The community is administered by the Millican-Ogden Community Association, which operates an outdoor swimming pool and an indoor hockey skating rink. The community hall has wheelchair access, a kitchen, lounge and a stage. The community association also hosts fitness and youth justice programs. Outdoor recreation is easy to come by, with fields Lynnwood Park, Jack Setters Park and Pop Davis Park, each having baseball and playing fields. The Bow River Pathway travels north through Ogden, a popular trail for hiking, jogging and biking. Across the river is the Golf Canada Calgary Centre 9-hole golf course and driving range, open to the public.</p>
<p>Also across the river is the Deerfoot Meadows shopping centre, with over 50 shopping boutiques, including Ikea, Best Buy and Michaels, and restaurants, including Ricky&rsquo;s All Day Grill, Le&rsquo;s Pho and Five Guys Burgers and Fries. Just up the hill from Deerfoot Meadows is the new location of the Calgary Farmer&rsquo;s Market, offering local and organic produce, from meat to vegitables and fruit, as well as beautiful artwork and accessories.</p>
<p>Ogden has a public elementary and community school, as well as a Catholic separate school.</p>
<h2>Ogden/Lynnwood Misc.</h2>
<p>Ogden/Lynnwood is located in Ward 9 and represented by Gian-Carlo Carra on the Calgary City Council. Provincially, Ogden/Lynnwood is in the Calgary-Fort riding, represented by Wayne Cao and federally in the Calgary Southeast riding, represented by Jason Kenney.</p>
