<h2 id="more">Albert Park/Radisson Heights</h2>
<p>With a long and glorious history of more than a century, the Albert Park/Radisson Heights is a spectacular residential community located within the south-east quadrant of Calgary. The neighborhood is surrounded by 17 Avenue SE to the south, Memorial Drive to the north, Deerfoot Trail to the west, and the community of <a href="/forest-lawn.php">Forest Lawn</a>&nbsp;to the east.</p>
<h2>Albert Park/Radisson Heights Real Estate</h2>
<p>Established back in 1910, the community in Albert Park/Radisson Heights possesses a distinctive old-world charm that sets it a class apart from the rest of the neighborhoods in Calgary. The Albert Park/Radisson Heights Real estate offers some of the most spectacular views of the enigmatic Bow River Valley and the majestic Canadian Rockies. Residents in Albert Park/Radisson Heights have a plethora of housing options ranging from single-family detached homes to apartments and condominiums that are regularly available at an average price of $350,000. Its ample open spaces and central location with respect to all major public amenities and recreational venues renders Albert Park/Radisson Heights one of the most desirable communities in SE Calgary.</p>
