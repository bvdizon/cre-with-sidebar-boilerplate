<h2 id="more">Discovery Ridge</h2>
<p>The southwest quadrant of Calgary features the scenic residential community of Discovery Ridge. The community is flanked by Glenmore Trail on the north and to the east by 69th Street. Griffith Woods Environmental Park occupies a large part of the southern border of Discovery Ridge. Huge green lung spaces and scenic views make this community a much-desired location to live in.</p>
<h2>Discovery Ridge Real Estate</h2>
<p>Development of Discovery Ridge real estate began in the 1990s, prior to which the area was an isolated valley referred to as &ldquo;Jackson&rsquo;s Valley&rdquo; or New Discovery by locals. The average sale price for homes in Discovery Ridge real estate is around $770,000. Options for homes are plenty and the pricing tends to be in the mid-high range for single family homes between $252,000 and $1,250,000. Discovery Ridge real estate feature architecturally designed modern homes with great amenities and serene, picturesque locales. Living in Discovery Ridge can give residents the best of both worlds in terms of amenities and a peaceful lifestyle.</p>
<h2>Discovery Ridge</h2>
<p>On the Southwest corner of Calgary at the Edge of Glenmore Trail and Sarcee Trail South, just minutes from Downtown Calgary lies the secluded Calgary real estate community of Discovery Ridge. Homes in Discovery Ridge enjoy a backdrop of pine trees and mountain views. The Elbow river rushes nearby and this charming area is reminiscent of an inner-city Canmore.&nbsp; Discovery Ridge homes range from small forest edge condominiums and field-backed villas to gorgeous Discovery Ridge estate homes. Discovery Ridge attracts an eclectic mix of white-collar estate owners to mountain bikers and hikers wanting that &ldquo;Canmore&rdquo; feel right in the city.</p>
<p>Whether you&rsquo;re after wilderness, whimsy or proximity to the core, you&rsquo;ll find it here in Discovery Ridge.</p>
<p>Discovery Ridge has a bevy of dining options including high-end eateries and quaint wine bistros. It's not hard to relax in Discovery Ridge, this unique part of Calgary it offers everything from wining and dining to hiking and wilderness biking.&nbsp;</p>
