<h2 id="more">Glamorgan</h2>
<p>Established in 1958, Glamorgan in South West Calgary, Alberta is a warm and welcoming residential community that is bound by the Sarcee Trail on the west, the Glenmore Trail on the South, 37 Street W on the east and the Richmond Road on the north. Represented by the Ward 6 councilor in the Calgary City Council, Glamorgan houses approximately 6500 people living across in a variety of dwellings.</p>
<h2>Glamorgan real estate</h2>
<p>While the community in Glamorgan comprises around 1200 single family detached homes, there are approximately 1100 units of townhouses and condos in the neighborhood. Located at a convenient 10-minute drive from Downtown Calgary, the Glamorgan real estate is well connected with a strategically planned network of pathways and streets. Regardless of whether you are first time home buyer or a luxury investor, Glamorgan offers a wide range of home styles and price points for you to choose from. You will be able to find your dream home in the neighborhood for an affordable $200,000 to a hefty $1 million as well. Since most of the residential properties were initially built in the 50s, a majority of them have undergone a myriad of upgrades and remodels in the last decade.</p>
