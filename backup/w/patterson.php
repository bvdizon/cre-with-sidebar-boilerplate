<h2 id="more">Patterson</h2>
<p>Established in 1983, Patterson is one of Calgary&rsquo;s oldest west side residential communities and also the most sought after among young homebuyers and investors alike. The neighborhood is also noteworthy for the Broadcast Hill Media Village that was located in Patterson Heights during the XV Olympic Winter games held in 1988.</p>
<h2>Patterson real estate</h2>
<p>Named after the Patterson family that owned a major chunk of the land in the neighborhood, the community in Patterson is perched atop a ridge overlooking the Edworthy Park and the Bow River valley and offers some of the most breathtaking views of the city. The Patterson homes for sale majorly comprise of older luxury homes that typically fall within the $1 million range. Alternatively, there are the more affordable condominiums and low-rise apartments that are available at a starting price of $200,000. With the famous shopping complex the Westhills Towne Centre located at a few minutes drive down south and the Western Recreation Centre offering a variety of sports and fitness facilities, the Patterson real estate provides adequate recreational facilities to its residents.</p>
