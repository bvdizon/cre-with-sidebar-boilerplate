<h2 id="more">Glendale</h2>
<p>With breathtaking views of the Rockies to the west, Glendale is a serene residential neighborhood located in the Southwest quadrant of Calgary. Bound by the 17th Avenue SW on the north, the Sarcee Trail and Signal Hill on the west and the Killarney and 37 Street SW on the east, Glendale is represented by the Ward 6 councilor in the Calgary City Council.</p>
<h2>Glendale real estate</h2>
<p>Established in 1955, the community in Glendale has a population of around 2700 that dwells in a variety of residential units ranging from row houses, single-family detached homes, apartments and even condominiums costing somewhere between $450,000 and $750,000. Some of the bungalows date back to the 50s and have luxurious back and front yards to boast of. The major highlight of the Glendale real estate is the Optimist Athletic Park that supports a plethora of sports activities including soccer, softball, field hockey, lacrosse, and football. Shaped like a hammer, this iconic park houses 3 sports fields, 1 little league diamond and a total of 8 ball diamonds.</p>
