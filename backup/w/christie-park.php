<h2 id="more">Things to do in and around Christie Park</h2>
<p>Christie Park is administer by the Strathcona Christie Aspen Community Association, along with Strathcona Park and Aspen Wood, and operates public tennis courts, an outdoor hockey and skating rink and a sports/playing field. The joint community centre for the three communities has a gym, commercial kitchen, lounge, meeting rooms, a sound system and an indoor fireplace, as well as a deck with a great view and a loft. The community association hosts a variety of programs, including sports, fitness, dance, senior programs and childcare. Westside Recreation Centre has a fitness centre and swimming pool open to the public.</p>
<p>South of Christie Park is Westhills Shopping Centre, with an assortment of shops, boutiques and family restaurants. Westhills also has a cinema, showing the latest movie releases. Restaurants include Montana&rsquo;s, Swiss Chalet and Moxies, along with various fast food restaurants. Christie Park is home to the Islamic Centre of Calgary.</p>
<p>The CBE administers a public elementary in the area, with other schools in the surrounding communities.</p>
<h2>Christie Park Misc.</h2>
<p>Christie Park in in Ward 6 and represented by Richard Pootmans on the Calgary City Council. Provincially, it is in the Calgary-West riding, represented by Ken Hughes and federally in the Calgary West riding, represented by Ron Anders.</p>
