<h2 id="more">Things to do in and around Rutland Park</h2>
<p>Across Crowchild Trail is Marda Loop Business Revitalization Zone, a popular Calgary outdoor shopping district with unique, owner operated boutiques, from clothing to accessories. Marda Loop also has several owner run pubs and restaurants, including Breakfast in the Loop (formerly the Independent Nellies) and Belmount Diner, both of which are famous for their all day Sunday brunches and friendly service; other well known restaurants include Dragon Gate Restaurant and the award winning Basil's Classic Grill &amp; Wine Bar.</p>
<p>The community is served by a public elementary school, with public junior highs and high schools being located in adjacent communities.</p>
<h2>Rutland Park Misc.</h2>
<p>Rutland Park is located jointly in Ward&rsquo;s 8 and 11 and represented by John Mar and Brian Pincott, respectively. Provincially, Rutland Park is in the Calgary-Elbow and represented by Alison Redford, current Premier of Alberta, and federally in the Calgary-Centre riding, with no representation following the resignation of Lee Richardson.</p>
