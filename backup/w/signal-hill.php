<h2 id="more">Signal Hill</h2>
<p>Located in the center of the Broadcast Hill in Calgary, the Signal Hill community comprises of two residential neighborhoods namely the Signal Ridge and the Sienna Hills. Established in 1986, the community is bound by the <a href="/springbank-hill.php">Springbank Hill</a> community on the west, the Glenmore Trail on the south, the 17th Ave on the north and the Sarcee trail on the east.</p>
<h2>Signal Hill real estate</h2>
<p>Originally known as the media village during the 1988 Winter Olympics, the community in Signal Hill was redeveloped with a variety of residential dwellings including single family detached homes and condominiums to fulfill the varying needs of the homebuyers and investors alike. With its close proximity to a host of recreational venues and public amenities and stunning views of the city, the Signal Hill real estate forms an ideal neighborhood for both young families as well as the retirees. Despite the popularity of the neighborhood, home prices are quite affordable with a majority of the housing units available at a little over the Calgary average.</p>
