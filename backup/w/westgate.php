<h2 id="more">Westgate</h2>
<p>A tranquil residential neighborhood located in the South West quadrant of Calgary, Westgate was established in the year 1957 and is currently represented by the Ward 8 councilor in the Calgary City Council. The neighborhood is surrounded by the Bow Trail and the Edworthy Park to the north, the Sarcee Trail on the west, 17 Avenue S on the south and the 45 Street W on the east side.</p>
<h2>Westgate real estate</h2>
<p>With a major chunk of its development happening in the 50s and the 60s, Westgate homes for sale primarily comprise of vintage style bungalows on wide lots and two story traditional housing units. Alternatively, there are a couple of townhomes and condominiums also available in the community in Westgate. Given the fact that most of the dwellings are more than half a century old, a majority of them have undergone a series of upgrades and remodels to achieve the contemporary style suited to the modern homebuyers and investors. While a typical two-storey housing unit will be listed around $600,000, the condos are a more affordable option at an average price of $250,000+. With a variety of recreational venues including shopping complexes, hockey rinks, parks and sports fields located within the Westgate real estate, the residents have plenty options for entertainment.</p>
