<h2 id="more">Aspen Woods</h2>
<p>Located in the southwest quadrant of Calgary, Alberta, Aspen Woods is bordered by 17 Avenue SW in the south and 12 Avenue and Bow Trail SW in the north. 85 Street SW runs along the eastern border, and towards the west is 101 Street SW. Being very close to Downtown Calgary makes Aspen Woods an attractive destination for a long-term dwelling.</p>
<h2>Aspen Woods Real Estate</h2>
<p>With an average asking price of $1,500,000, homes in Aspen Woods real estate feature luxurious single-family homes in the setting of lush greenery in the form of evergreen trees and well-maintained lawns. Although annexed to the city of Calgary in 1956, Aspen Woods real estate was developed in 2001. Although a majority of the real estate is comprised of spacious single-family homes, there are condos and townhouses as well. With Downtown Calgary being just an 18-minute drive away, Aspen Woods is an ideal place for those who work in the city.</p>
<h2>Aspen Woods</h2>
<p>Aspen Woods has a number of lowrise apartments and townhouses, going from the mid $200 000 to the more luxury condo&rsquo;s around $2 M. Detached single-family homes are widely available, between a range of the high $500 000 to the high $900 000. Aspen Woods also has a large portion of luxury homes and acreages, in the low million-dollar range to as high as $11 M.&nbsp;</p>
<p><span class="indent">&nbsp;</span>Aspen Woods homes retain much of the Rocky Mountain charm but afford the conveniences of a contemporary, stylish inner-city locale. Just west of Sarcee Trail, the area is nestled between the downtown core offering gorgeous city views and quick commutes and the picturesque foothills of the Rocky Mountains. New homes in Aspen Woods and surrounding area sustain high property values and a thriving community. Architectural styles vary, offering something for every taste &ndash; mid-range townhomes and single-family residences, elegant New York style townhomes with private elevators, mountain view luxury estate homes and most everything in-between. Aspen Woods presents discreet, tree-lined lanes, leading both to its grand estates and its convenient upscale shopping centers.</p>
<h2>Things to do in and around Aspen Woods</h2>
<p>Aspen Woods is administered by the Strathcona Christie Aspen Community Association, along with Strathcona Park and Aspen Wood, and operates public tennis courts, an outdoor hockey and skating rink and a sports/playing field. The joint community center for the three communities has a gym, commercial kitchen, lounge, meeting rooms, a sound system and an indoor fireplace, as well as a deck with a great view and a loft. The community association hosts a variety of programs, including sports, fitness, dance, senior programs, and childcare. Westside Recreation Centre has a fitness center and swimming pool open to the public.</p>
<p>To the southeast of Aspen Woods is the Westhills Shopping Centre, which has a collection of shops, restaurants and medical facilities (dental and doctor clinics and pharmacies) and a multiplex theatre showing the latest releases. Family restaurants are easy to come by, with a Montana&rsquo;s, Moxie&rsquo;s Grill and Swiss Chalet, as well as a number of fast food restaurants.</p>
<h2>Aspen Woods Misc.</h2>
<p>Aspen Woods is located in Ward 6 and represented by Richard Pootmans on the Calgary City Council. Provincially, it is located in the Calgary-West riding and represented by Ken Hughes and federally in the Calgary West riding, represented by Ron Anders.</p>
