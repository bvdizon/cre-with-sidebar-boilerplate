<h2 id="more">Things to do in and around Rosscarrock</h2>
<p>The Rosscarrock Community Association, with a community centre complete with a meeting room, stage, and standard kitchen, administers Rosscarrock. The Association also operates an outdoor skating rink and hosts bingo, senior programs, and youth justice programs. Community meetings are held the fourth Thursday of every month. Westbrook Mall is located in Rosscarrock and has various shopping boutiques, a food court, and restaurants, including a Boston Pizza and Rusty Cage Bar and Grill. Recreation-wise, Rosscarrock is in close proximity to Glendale Meadows, Edworthy Park, Douglas Fir Trail and the Shaganappi Point Golf Club.</p>
<p>The CBE operates a public elementary school, with a Catholic, late French immersion school also in the area.</p>
<h2>Rosscarrock Misc.</h2>
<p>Rosscarrock is located in Ward 6 and represented by Joe Connelly on the Calgary City Council. Provincially, Rosscarrock is in the Calgary-Currie and represented Christine Cusanelli and in the Calgary Centre federal riding, with no representation after the resignation of Lee Richardson.</p>
<p>Several medical clinics are located in the Westbrook Mall, from dentists to family doctors, as well as a chiropractic office and radiology consultants</p>
