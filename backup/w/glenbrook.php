<h2 id="more">Glenbrook</h2>
<p>Just north of Glamorgan in the South West Calgary, lies the quiet and picturesque neighborhood of Glenbrook. Established in 1958, Glenbrook is bound by the Sarcee Trail on the west, the Richmond Road on the south, 37 Street W on the east and 26 Avenue S on the north.</p>
<h2>Glenbrook real estate</h2>
<p>Home to an eclectic mix of residential dwellings ranging from single-family detached homes, townhouses to condominiums and low-rise apartments, the community in Glenbrook offers a divine combination of the relaxed appeal of the suburbs and the vibrant life of the city. The few residential units that date back to the 60s have been recently renovated and remodeled to cater to the needs of the modern lifestyle and are available at anywhere between $200,000 and $750,000. From cozy ranch-style bungalows to contemporary duplexes, there is something for everyone in this family-friendly neighborhood. The Glenbrook real estate also offers a plethora of recreational facilities including a swimming pool, an indoor ice skating rink, golf courses and a myriad of shopping complexes to keep your busy and entertained.</p>
