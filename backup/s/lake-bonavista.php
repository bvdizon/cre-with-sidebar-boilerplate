<h2 id="more">Lake Bonavista Homes &ndash; Lake Bonavista Estates</h2>
<p>Lake Bonavista was the first community in Canada to be built surrounding a man-made lake, acting as a prototype for
    further lake community developments in the country. Surrounding two beautiful lakes (Lake Bonavista to the North and
    Lake Bonaventure to the South), Lake Bonavista homes are located nearby the beautiful Fish Creek Provincial Park and
    crested by the majestic prairie sky. Not only a wonderful place to raise children, but also a hot spot for outdoor
    recreation with easy connectivity to shopping and services.</p>
<h2>Things to do in and around Lake Bonavista</h2>
<p>Lake Bonavista is located directly north of Fish Creek Provincial Park, a section known as Glennfield. Three group
    use areas are located at Glennfield, which can be booked for family or other group BBQ&rsquo;s. Surrounding these
    areas are firepits for hot-dog roasts and picnics. Fish Creek is also covered with pathways and hiking trails and is
    an excellent recreational and nature area, with a variety of wildlife and plant life. Lake Bonavista homes have two
    lakes nearby, the main one being Lake Bonavista, the second being Lake Bonaventure, a private use lake. Lake
    Bonavista residents receive a membership card and are welcome to use the Lake as they please. The lake is a pleasant
    place for recreational activity, whether it is playing tennis in the courts, swimming in the water or boating around
    the lake.</p>
<p>Entertainment is not hard to come by, with Southcentre Mall being across Anderson Road. Avenida Shopping Centre is
    located along the length of Lake Bonavista, with a limited number of retail stores ranging from fashion to food.
    Avenida Art Gallery is a major attraction in the shopping center, harboring a number of artworks and styles of
    different artists. Canyon Meadows Cinema shows new movie releases at cheap prices, making it a popular destination
    for moviegoers. Fun N&rsquo; More is a child-themed entertainment center, with indoor play structures, arcade, party
    rooms, and more. For dining, Boston Pizza is located in the Avenida Shopping Centre, with both a family lounge and a
    bar.</p>
<p>Lake Bonavista has three public elementary schools and a junior high, as well as a Catholic elementary and junior
    high.</p>
<h2>Lake Bonavista Misc.</h2>
<p>Frank McCool Arena offers sports programs for youth, from basketball to hockey to curling. Adjacent to the Arena is
    the Frank McCool athletic park, where group soccer and ultimate Frisbee league tournaments occur during the summer.
    Lake Bonavista homes have four churches in the area, a Baptist, Evangelical, Anglican, and Catholic.</p>
<h2>Lake Bonavista Estates</h2>
<p>Lake Bonavista Estates is situated south of Calgary, Alberta. The southern border of Lake Bonavista is marked by the
    Canyon Meadows Drive SE and the Fish Creek Provincial Park. The Macleod Trail runs along the western border while
    Acadia Drive SE is towards the east. The Anderson Road SE and Willow Park neighborhood flank the north side of Lake
    Bonavista Estates.</p>
<h3>Lake Bonavista Estates Real Estate</h3>
<p>Developed in the 1960s by Kelwood Corp, the Lake Bonavista real estate includes three different levels of properties.
    Lake Bonavista Estates among these features premier properties with all amenities to suit the upscale lifestyles.
    The community includes a 52-acre man-made lake built in 1968 to add to the natural beauty of the location. The lake
    and the Fish Creek Park provide a host of recreational opportunities besides offering pleasant views from the homes.
    The highest price for homes in Lake Bonavista Estates is $4 million, while the average price is $685,000.</p>
