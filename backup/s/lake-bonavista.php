<h2>Bonavista Downs</h2>
<p>Lake Bonavista Downs can be found in the southeast part of Calgary. This tiny triangle-shaped community has
    Queensland Downs to its east, Lake Bonavista to its west and Maple Ridge to its north. The development of Bonavista
    Downs began in the year 1971.</p>
<h3>Bonavista Downs Real Estate</h3>
<p>The unique thing about Bonavista Downs Real Estate is that it features only single-family homes. While living in the
    community of Bonavista Downs, you can enjoy quick and easy access to the Fish Creek Provincial Park and to the
    pathway. The population of Bonavista Downs as recorded in 2011 was 11,557 and the count of homes stood at 4,476. The
    average price of a home in the community is approximately $487,000.</p>
