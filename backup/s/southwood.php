<h2 id="more">Southwood</h2>
<p>Nestled within the south west quadrant of Calgary, Southwood is quaint residential neighborhood that promises a great
    quality of life for its residents. The community is bounded by the Anderson Road on the south, the 14th Street SW on
    the west, the Macleod Trail on the east and the 98th Avenue on the north.</p>
<h2>Southwood Real Estate</h2>
<p>Primarily a single family residential neighborhood, the community in Southwood was established back in 1959. Since a
    majority of the mid century bungalows have been here for near about half a century, a series of renovations have
    been made to keep them aligned with the demands of the contemporary homebuyer and investors. Homes for sale in
    Southwood comprise of single family detached and attached units with a few recently developed apartment
    condominiums. Property prices in Southwood Real Estate are pretty affordable and range from somewhere along the
    lines of $200,000 to as high as $600,000.</p>
