<h2 id="more">Things to do in and around Somerset</h2>
<p>The Somerset-Bridlewood Community Association administers Somerset, with the Resident&rsquo;s Association maintaining
    a Water Park, Volleyball court and Tennis Courts. In nearby Shawnessy is a shopping centre, with retail stores,
    restaurants and pubs/clubs. Quick fast food restaurants are easy to come across, but the area also has more fine
    dining restaurants as well, including Moxies, Plaka Greek Restaurant and Hong Kong Garden. Hudson&rsquo;s Bar and
    Grill is a pub and club and is a popular nightlife destination. If not up for a night on the town, Empire Theatres
    operates a theatre showing the newest releases. Well known shops are also in the area, from Winners to Sports Check,
    and even big box stores, such as Walmart and Canadian Tire.</p>
<p>The CBE operates an elementary school in Somerset, with a highschool nearby. Bishop O&rsquo;Byrne High School is a
    special case, as it is a high school, YMCA and public library built in one. A private Christian charter school is
    also located in Somerset.</p>
<h2>Somerset Misc.</h2>
<p>Somerset is located in Ward 13 and represented by Somerset is serviced by the Somerset-Bridlewood station, the last
    stop on the Dalhouse line. Bridlewood is located in Ward 13 and represented by Diane Colley-Urquhart on the Calgary
    City Council. Provincially, Bridlewood is in the Calgary-Lougheed riding, represented by Dave Rodney and federally
    in the Calgary Southwest riding, the current seat of Prime Minister Stephen Harper.</p>
