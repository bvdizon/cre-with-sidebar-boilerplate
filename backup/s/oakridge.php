<h2 id="more">Oakridge</h2>
<p>Located immediately east of the Weaselhead Park Natural area, and south of the Glenmore Reservoir, Oakridge is a
    mature family-oriented community in the South West quadrant of Calgary. The community is flanked by the Tsuu
    T&rsquo;ina first nation reserve on the west, and the 90 Avenue S on the north.</p>
<h2>Oakridge Real Estate</h2>
<p>Originally a part of the Municipal District of Rocky View, the community in Oakridge was annexed to the city of
    Calgary in 1956. Established in 1968, the Oakridge Real Estate offers an eclectic mix of housing options including
    single family detached units and townhomes that are surrounded by beautiful tree-lined streets and walking trails.
    Homes for sale in Oakridge are available at an affordable starting price of around $200,000 to as high as a million
    for the luxury estate homes. While The Oakridge Co-op Shopping Plaza offers adequate amenities to cater to your
    everyday needs, the South Center Mall takes care of your more expensive shopping requirements.</p>
