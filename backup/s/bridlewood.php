<h2 id="more">Bridlewood Homes</h2>
<p>Established in 1998, Bridlewood is a new neighbourhood compared to most other neighbourhoods in Calgary. Built with
    open space and next to a wetland, Bridlewood is lush, green, and full of wildlife; a great community for those with
    a family and for those who are active and love the outdoors.&nbsp;</p>
<h2>Things to do in and around Bridlewood</h2>
<p>The Somerset-Bridlewood Community Association administers Bridlewood, with the Resident&rsquo;s Association
    maintaining a water park, Volleyball courts, and tennis courts, all for public use. A five-minute drive away is
    Shawnessy&rsquo;s shopping district, complete with restaurants, pubs and shopping boutiques. People can find various
    familiar fast-foods for quick, on-the-go meals, as well as a selection of dine-in restaurants, including a Moxies,
    Ricky&rsquo;s All Day Grill and Zeller&rsquo;s Restaurant. Hudson&rsquo;s Bar and Grill is a mixture of a pub and
    nightclub, with the main floor being converted into a dance floor during late nights on weekends. Clothes shopping
    is the dominate boutique, with a Winners, Sports Check, and Warehouse one Jeans, among others. Bishop O&rsquo;Byrne
    High School is a unique building, as it is a High School, YMCA and public library built into one, offering
    recreation, education and a quiet reading place for all ages.</p>
<p>Bridlewood has a public elementary school, a Catholic school, and a private Christian school.</p>
<h2>Bridlewood Misc.</h2>
<p>Bridlewood is located in Ward 13 and represented by Diane Colley-Urquhart on the Calgary City Council. Provincially,
    Bridlewood is in the Calgary-Lougheed riding, represented by Dave Rodney and federally in the Calgary Southwest
    riding, the current seat of Prime Minister Stephen Harper. The Somerset-Bridlewood LRT station services Bridlewood
    and is the last stop on the Dalhousie line.</p>
