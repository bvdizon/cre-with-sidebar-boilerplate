<h2 id="more">South Calgary Maple Ridge</h2>
<p>A family-friendly neighbourhood, Maple Ridge is a fantastic place to raise a family. The big Alberta sky flaunts her
    beauty over the lush green grass and trees of the community. Maple Ridge is a mixed residential and recreational
    community, with easy access to schools, entertainment and recreation.</p>
<h2>Things to do in and around Maple Ridge</h2>
<p>Maple Ridge is home to Maple Ridge Public Golf Course overlooking the Bow River Valley, making it an ideal and scenic
    place for people who love an occasional 9-hole game. At the end of Southland Drive is the Bow River Valley and
    Southland Dog Park, making Maple Ridge an excellent location for dog owners and avid walkers/runners. The community
    is also located a short drive from Fish Creek Park. South Centre Mall is located in the adjacent Willow Park
    community, along with the Trico Leisure Centre, Fish Creek Public Library and Anderson LRT Station, granting easy
    access to downtown via the C-train and various southeast and southwest communities via the large quantity of bus
    routes.</p>
<p>Maple Ridge is home to a public elementary and middle school, with a public high school in neighbouring Acadia. There
    is also a private French immersion school in Willow Park.</p>
