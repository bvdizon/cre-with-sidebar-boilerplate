<h2 id="more">Evergreen Homes</h2>
<p>Evergreen is separated into two sections, the older, established homes being built in the 1980's and the newer homes
    being constructed in 1999 or later, each section having their own separate Community Association and street naming
    patterns. Located on the south border of Fish Creek Provincial Park, Evergreen is within walking distance of
    beautiful vegetation and outdoor recreation.</p>
<h2>Things to do in and around Evergreen</h2>
<p>Fish Creek Provincial Park is located in the north of the community and is an example of an urban setting being able
    to be ecologically friendly. Fish Creek is perfect for jogging, walking, picnics and fires. Full of wildlife and
    plant life, Fish Creek is a popular destination for those wishing to escape the urban city and escape into a garden
    paradise. Nearby Shawnessy has a shopping district, complete with restaurants, boutiques, and a cinema. The YMCA
    also has a facility in Bishop O&rsquo;Byrne in Somerset, meeting all fitness needs with a weight room, a gym, and a
    swimming pool.</p>
<p>Evergreen recently had a public elementary school opened, from K-4.</p>
<h2>Evergreen Misc.</h2>
<p>Evergreen is located in Ward 13 and represented by Diane Colley-Urquhart on the Calgary City Council. Provincially,
    the community lies in the Calgary-Lougheed riding and represented by Dave Rodney and in the federal riding of
    Calgary Southwest and represented by Stephen Harper, current Prime Minister of Canada. Evergreen is serviced by the
    Shawnessy LRT Station on the Somerset-Bridlewood/Dalhousie line.</p>
