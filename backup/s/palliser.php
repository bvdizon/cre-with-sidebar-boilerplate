<h2 id="more">Palliser</h2>
<p>Famous for the Tom Brook Athletic Park located within its premises, the Palliser community is a charming residential
    neighborhood in the South West quadrant of Calgary in Alberta. The community is bound by 24 Street SW on the west,
    the Southland Drive on the south, 19 Street SW on the east and the 90 Ave SW on the north.</p>
<h2>Palliser Real Estate</h2>
<p>The community in Palliser got its name from an early geographer and explorer from Western Canada called John
    Palliser. The roots of Palliser Real Estate were laid in the 1960s when the very first buildings in the community
    came into being. Located immediately south of the Glenmore Reservoir, the Palliser neighborhood offers a range of
    housing options from detached houses to townhomes and even apartments. Homes for sale in Palliser are readily
    available at a starting price of around $300,000 and go up to $900,000 for the premium proprieties. There is a
    number of shopping complexes such as the Chinook Centre and the Southcenter Mall within close vicinity of Palliser
    that cater to the everyday needs of the residents.</p>
