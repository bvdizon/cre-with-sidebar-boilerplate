<h2 id="more">Diamond Cove</h2>
<p>A cozy residential neighborhood located in South East Calgary, Diamond Cove is bound by the Deerfoot Trail on the
    north, the Bow River on the east, the Queensland on the south and the Bow Bottom Trail on the west.</p>
<h2>Diamond Cove Real Estate</h2>
<p>Originally established as an estate community in 1989, the Diamond Cove Real Estate was officially recognized by the
    government only in 1991. With a dedicated entrance via the Bow Bottom Trail, the community in Diamond Cove offers
    some of the most spectacular views of the Bow River Valley. Homes for sale in Diamond Cove comprise single family
    dwellings, with the a few recently developed apartment condominiums. A typical residential unit in Diamond Cove can
    be yours for a starting price of around $400,000, with the more expensive properties priced as high as $800,000.</p>
