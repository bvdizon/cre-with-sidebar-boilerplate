<h2 id="more">Things to do in and around Millrise</h2>
<p>Millrise is administered by the Millrise Community association, which operates a hockey rink and an outdoor skating
    rink. Millrise Park in the north of the community has two baseball fields; one large and one medium sized one. Fish
    Creek Provincial Park is located North of Millrise, past Shawnee Slopes, and is Calgary&rsquo;s most popular outdoor
    parks. Perfect for family picnics, bon fires, jogging, and short hikes,. Nearby Shawnessy has a shopping centre with
    various boutiques, restaurants and big box stores. Empire Cinema shows the newest movie releases. A YMCA is operated
    in nearby Somerset, complete with weight room, gyms and swimming pool open to the public.</p>
<p>A Catholic Elementary/Junior High school operates in the area, with public schools being easily accessible from
    Millrise.</p>
<h2>Millrise Misc.</h2>
<p>Millrise is located in Ward 13 and represented by Diane Colley-Urquhart on the Calgary City Council. Provincially,
    Millrise is in the Calgary-Lougheed riding, represented by Dave Rodney and federally in the Calgary Southwest
    riding, the current seat of Prime Minister Stephen Harper. The Fish Creek-Lacombe LRT station services Millrise and
    goes as far north as <a href="/dalhousie.php">Dalhousie</a>, passing along popular business areas and commercial
    districts.</p>
