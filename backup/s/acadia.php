<h2 id="more">Acadia</h2>
<p>Acadia is a peaceful community located in the southeastern part of Calgary in Alberta. To the west of Acadia is
    Macleod Trail and it is eastbound by the Bow River. Fairmount Drive is the main street of the neighborhood and also
    the hub of all business activity.</p>
<h2>Acadia Real Estate</h2>
<p>The community of Acadia was established in 1960. According to the 2012 consensus, the population of the Acadia was
    10, 705 and the number of homes were recorded at 5,053. The development of the annexed land in Acadia began in 1960.
    Today, the community has a mix of residential, recreational and land uses. Even though Acadia is exposed to plenty
    of retail bustle, it continues to be a peaceful and friendly neighborhood and loved by both large and small
    families. Acadia real estate comprises a variety of different homes including single family homes, multi-family
    homes and condominiums.</p>
<h2>Things to do in and around Acadia</h2>
<p>Acadia is located conveniently close to shopping centres, such as Deerfoot Meadows to the east, and is also in
    between SouthCentre Mall and Chinook Centre. Southland and Heritage LRT stations are located across McLeod Trail and
    provide train services to Downtown and Somerset/Bridlewood. LaserQuest is a family oriented laser tag arena located
    just across McLeod Trail.</p>
<p>Acadia has three established public schools, an elementary, middle and senior high school, along with one private
    school and one Catholic school.</p>
<h2>Acadia Misc.</h2>
<p>Many recreational activities are available at the Acadia Recreational Complex (ARC), including Hockey, curling and
    basketball.</p>
