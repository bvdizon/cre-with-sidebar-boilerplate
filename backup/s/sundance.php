<h2 id="more">Things to do in and around Sundance</h2>
<p>Sundance is part of the Mid-Sun Community Association (short for Midnapore-Sundance), which has a community hall
    complete with a gym, standard kitchen, a lounge and meeting rooms. The association offers sports programs, fitness
    programs, dance classes, senior programs and childcare. In the heart of the community is Lake Sundance, access
    available for all residents, who can bring friends and family for a lovely day in the sand and water, or take
    advantage of using the playground, tennis courts or the green grass. Bordering the community from the east is Fish
    Creek Provincial Park, a beautiful, scenic park full of lush vegetation and wildlife. Excellent for hiking, jogging
    and biking/rollerblading, Fish Creek is a popular destination to escape the urban areas and find peace. Fish Creek
    is also home to the public Sicome Lake, where people are free to play and swim.</p>
<p>On the other side of Macleod Trail is the Shawnessy shopping district, offering a varied selection of retail shops,
    from electronics to clothing and accessories. The area also has a number of restaurants. Empire Theatres has a
    cinema in the district, showing the latest releases and hit movies. Bishop O&rsquo;Byrne High School is unique in
    the fact it is also home to a YMCA and a Calgary Public Library, both of which are open to the public.</p>
<p>Sundance is home to a public elementary, junior high and high school.</p>
<h2>Sundance Misc.</h2>
<p>Sundance is home to the South Calgary Health Centre and the South Calgary Medical Clinic for easy access to medical
    need in the chance something happens.</p>
