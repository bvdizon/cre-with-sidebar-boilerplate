<h2 id="more">Things to do in and around Queensland</h2>
<p>Queensland is administered by the Queensland Community Association, which operates tennis courts, outdoor skating
    rink, and a sports/playing field as well as programs including Bingo (at Jackpot Junction), sports, fitness, youth
    justice, senior programs and childcare (preschool). The community hall has wheelchair access, a gym, standard
    kitchen, meeting rooms, stage, and outdoor BBQ. Queensland is located overlooking Fish Creek Provincial Park and the
    Bow River Pathway, both excellent and popular scenic areas for jogging, hiking, and biking, as well as a place to
    see nature and get away from the urban setting. Fish Creek is also popular for its picnic areas along the Bow River,
    as well as the wildlife and vegetation.</p>
<p>In nearby Deer Valley, Deer Valley Shopping Centre has a home, incorporating both an indoor and outdoor area.
    Currently, the Deer Valley Mall is undergoing reservations to add new shops and acres to the building. Deer Valley
    Mall has a selection of owner-operated shops, while outside has familiar places, such as Co-op, Sobey&rsquo;s,
    Starbucks and Dairy Queen. There is also the Winking Owl Pub, a popular Irish pub.</p>
<p>The CBE operates two elementary schools and one junior high.</p>
<h2>Queensland Misc.</h2>
<p>Queensland lies in ward 14 and is represented by Peter Demong on the Calgary City Council. Provincially, Queensland
    is in the Calgary-Fish Creek riding, represented by Heather Forsyth and federally in the Calgary Southeast riding,
    represented by Jason Kenney.</p>
