<h2 id="more">Walden</h2>
<p>Looking for a relaxed and &lsquo;green&rsquo; lifestyle with a host of modern conveniences at your doorstep? Well,
    then the Walden community is the ideal residential neighborhood for you to settle down in the south-east quadrant of
    Calgary in Alberta.</p>
<h2>Walden Real Estate</h2>
<p>Established by the real estate giant Genstar, the community in Walden was approved by the Calgary City Council in
    2007, with the first leg of development commencing in early 2008. Sprawled across an impressive 620 acres of land,
    the Walden Real estate was named after Henry David Thoreau&rsquo;s famous book called Walden. Homes for sale in
    Walden comprise stylish and elegant architectural designs that are highly sought after among the younger population.
    You can choose from a large variety of housing options from single family homes to secondary suites and condos
    listed within the price range of around $300,000 to the mid 700s. Recreational opportunities abound in this area,
    owing to its close proximity to a host of public amenities such as restaurants, cafes, malls, golf course, parks and
    so on.</p>
