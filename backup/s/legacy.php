<h2 id="more">Legacy</h2>
<p>Nestled between the breathtaking Bow River Valley and the Macleod Trail SE along the southern boundary of the city,
    Legacy is a charming residential neighborhood located in South East Calgary. The community lies south of the
    community of Walden and houses the Hamlet of Heritage Pointe in the MD of Foothills No.31.</p>
<h2>Legacy Real Estate</h2>
<p>Sprawled across an impressive 1,100 acres of land, the community in Legacy is a family oriented neighborhood that
    features enormous open spaces, sidewalks, and pathways. From some of the most famous restaurants in the city to
    state of the art fitness centers, golf courses, and athletic facilities, the Legacy Real Estate has everything that
    you need to lead a comfortable life in the suburbs. Homes for sale in Legacy comprise townhomes, single family
    detached units and apartment condos that can be yours for a starting price of around $200,000, with the high-end
    properties available for as high as mid 800s.</p>
