<h2>Things to do in and around Willow Park</h2>
<p>It is located within relatively close distance to Fish Creek Park and the Bow River Valley. Shopping outlets are
    located in close proximity, with South Centre mall being located in the community and Chinook Centre being only
    three stops away by LRT from Anderson Station. Willow Park is also home to the Trico Leisure Centre (formerly Family
    Leisure Centre). An abundance of dinner and entertainment, including nearby restaurants Milestones, Boston Pizza and
    Governor&rsquo;s Pub. Willow Park Golf Course is a private golf and country club and extends the length of the
    community, with membership costing $35 000, though the course offers 2 year and 5-year payment options. As an
    alternative, across Acadia Drive, is Maple Ridge Public Golf Course for those who enjoy playing the occasional
    9-holes.</p>
<p>Willow Park has easy access to both public and private schools, including one that offers French immersion and a
    designated school for the arts.</p>
